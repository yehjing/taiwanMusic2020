    <h3>貳、國人創作與發表</h3>
    <p><strong>一、全年度的國樂創作</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      現代國樂的發展奠基在當代創作的基石上，鼓勵臺灣作曲家為臺灣這塊土地留下當代的音響紀錄是國樂發展重要的課題，TCO團長鄭立彬（2019）常說的一句話：<span class="bkt">「國樂發展的關鍵在作品。」</span>（頁64）2019年國人在國樂創作方面交出了亮麗的成績，委創樂曲有103首，包含3首套曲；編曲或移植有9首，共計112首（詳如附錄），遠超過2018年的84首，是臺灣國樂界共同努力的成果。
    </p>
    <p><strong>（一）委創的來源</strong></p>
    <p><strong>1.</strong><strong>委創來源的主體－職業樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;職業樂團為了年度音樂會的需求，會邀請作曲家為節目量身訂製樂曲，也因為他們擁有較多的資源，才有能力邀約稿件。今年除了南市國沒有新創樂曲外，TCO全年共有27首委創樂曲、1首移植樂曲，其中國人創作23首、移植1首；NCO全年共有12首委創樂曲，其中10首為國人創作；桃市國全年共有6首委創樂曲，全部都是國人創作；高市國全年有10首委創樂曲，其中7首為國人創作。總計2019年職業樂團共委託創作了55首樂曲，其中國人創作46首，約為83.64%，國人創作比例相當高。以全臺103首的國人創作樂曲，職業樂團所委創的46首，占44.66%，是委託國人創作的主要來源。
    </p>
    <p><strong>2.</strong><strong>委創來源的枝幹－民營樂團、社會團體、專業系所</strong></p>
    <p>（1）民營樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;民營樂團雖然經費不如職業樂團寬裕，除了從自有經費中撥付外，也可藉由向公部門提出補助申請，來支援委創所需要的經費。2019年民營樂團委創的單位有：小巨人絲竹樂團、台灣愛樂民族管絃樂團、台南樂集、薪橋國樂團、先嗇宮民族管弦樂團、心籟絲竹室內樂團、琵琶雅集、台灣揚琴樂團等團體。一共委託創作22首，國人創作有18首，約占整體的17.48%。其中心韻有4首；小巨人、台南樂集、琵琶雅集各有3首，是民營樂團中委創較多的團隊。
    </p>
    <p>（2）社會團體</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;中華民國國樂學會與台灣揚琴發展協會兩個社會團體，都意識到創作新樂曲對臺灣國樂發展的重要性，並且讓國內優秀資深作曲家及有志於創作且具潛力的年輕作曲家們，共同為臺灣這塊土地用音符留下美好的聲音而創作不同類型的作品。國樂學會委約了10首國人創作；揚琴協會則以委約及徵選兩種方式，為臺灣的揚琴作品增加了12首國人創作。2個團體在2019年共委託國人創作了22首樂曲，占整體的21.36%，對臺灣的創作環境提供了很大的創作空間。
    </p>
    <p>（3）專業系所</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在大專校院委創樂曲方面，2019年有3首，都是臺藝大中國音樂學系年度系展的邀約。該系每年在國家音樂廳舉辦系展，為了鼓勵同學們能夠在主修樂器上更加精進，都會藉由比賽挑選2位表現最優異的同學，委請作曲家為他們量身創作樂曲。多年來，已經累積了不少樂曲，並且其中的作品，還被選為職業樂團比賽、招考新進團員的指定曲目，如：任重的二胡協奏曲《島祭》，就是一個很好的例子。
    </p>
    <p><strong>3.</strong><strong>委創來源的綠葉－個人邀約與創作</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;個人的邀約與創作，如翠綠的葉子為國樂創作的環境增添了不少的亮點。2019年個人邀約的國人創作作品有6首，個人自行創作的有8首，其中楊杰儒在個人創作發表會中，發表了5首樂曲。許子薇、陳怡蒨和陳譽陞各自創作了一首樂曲，在自己的音樂會中演奏。
    </p>
    <p><strong>（二）委創的內容</strong></p>
    <p><strong>1.</strong><strong>節慶應景，描繪入聲</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019開春，高市國即以「豬事大吉
      四海迎春」為題，邀請了9位作曲家以農曆新年為題，發揮個人的創意，其中國人創作有7首。林子倫創作的《初歲隨想》，樂曲共分為四段，演出一開始由管樂吹出磅礡的序曲，後樂團接至輕快的快板片段，此曲最有趣的部分是自由板時各聲部模仿鞭炮聲，既豐富了樂曲的整體音效，也將樂曲承接至廣板，象徵新的一年，一切又再重新開始。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      鄭光智《火旁龍》以自幼的記憶，結合火旁龍的六個步驟，運用耳熟能詳的客家民謠「天公落水」及「糶酒」的片段旋律，結合北管鑼鼓、客家八音與現代作曲技法，描繪栩栩如生「火旁龍」的景緻。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      任重創作的笛協奏曲《年年》，以〈年貨大街〉、〈兒時的回憶〉及〈年年〉為題，為自由三段體的曲式。第一段以2/4拍為主要節奏，各聲部間以卡農和競奏方式為主。第二段為抒情的敘事段落，笛子以白鷺鷥素材拆解，連續六連音的旋律拆解，段末完整再現白鷺鷥主要旋律後七音結束，稍留惆悵感。第三段為頑固節奏及四個主要和弦構成，以流行樂編曲的模式運用在國樂團上，與前二段落形成對比。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      這些樂曲，作曲家都巧妙地運用了審美中的想像與聯想，以描繪性的手法將年節的景象編織成畫面，讓欣賞者得以透過聲響的變化，進入到作曲家的心靈世界。
    </p>
    <p><strong>2.</strong><strong>傳統取材，創新再現</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以在地素材為根，運用現代創作手法為器，使創作的樂曲既有傳統的養分，又能以嶄新的風貌呈現，是當今許多作曲家所追求的目標。素材來源有選自原住民歌謠、北管曲牌、客家民謠等。在2019年11月25日國樂學會所舉辦的年度創作發表會的10首樂曲中，就有4首各自採用了泰雅族、阿美族、布農族的歌謠為創作素材，譜寫出的國樂曲目，1首以北管曲牌為素材創作的樂曲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      鄭光智《泰雅之歌》以三首泰雅族歌謠「出草歌」、「收割歌」、「紡織歌」為元素創作，描繪代表泰雅族人的「出草」、「祖靈」、「紋面」、「彩虹」、「射日」，這些泰雅族人的精神、信仰與傳說。運用其特色三音、四音組織堆疊，使音樂製造出神秘遠古、精神抖擻的力量。（國樂學會，2019，頁15）樂曲開始以紙插在阮的弦中演奏，來模仿口簧的聲響，以笛子的旋律代表歌唱；快板部分氣氛熱烈，運用大量變化拍號，產生節奏律動的多變；慢板部分則舒緩而優美，整體而言是一首技巧性頗高的樂曲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      何立仁《輕快的腳步》以臺灣原住民「布農族」的傳統歌謠&ldquo;mudanikata&rdquo;作為創作的主要素材，（國樂學會，2019，頁9）以不同方式的變奏手法呈現(出遊/回家)的心境。在創作手法上除了展現布農族大部分為「大調」的音樂特點，更想表現&ldquo;mudanikata&rdquo;這首歌謠背後的心境，因為以布農族語&ldquo;mudanikata&rdquo;而言，可以是出遊，也可以是回家，這個意涵帶給了作曲者很多的想像，進而思考這個詞所代表的也許是一種心境，當要出門或是旅程結束要回家，也許都是類似的心境，故用了不同的速度及變奏手法表現這樣的心境!
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      張永欽《美麗的家鄉》取材自許常惠於1967年民歌採集運動當中所收錄的四首花蓮縣阿美族的音樂：「讚美太巴塱之歌」、「富田村快樂頌」、「美好的苓雅村」與「古歌」。（國樂學會，2019，頁13）因為這首樂曲由四首阿美族歌謠串連而成，因此也分為四段：第一段：帶有浪漫的情調，運用弦樂聲響，和聲豐富極有色彩性。第二段：由管樂主奏，音響飽滿，轉調之後以快板呈現。第三段：笛子主奏引入第三首，後由弦樂演奏轉調之後的主題，配器上帶有一些日本音樂的風格。第四段：嗩吶帶入雄壯的主題，與樂隊進行問答式的演奏，帶有明顯的小調風格。整首樂曲段落銜接自然，而以這樣串聯式的方法呈現，作曲家表示，是受到了關廼忠《藍色的思念》的啟發。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      黎俊平《社戲》以北管曲牌【二凡】作為素材，經過轉化後成為樂曲的主旋律。樂曲不是以傳統的擊樂樂段來表現熱鬧的氣氛，相反的，樂曲的擊樂部份除了帶有北管特色的樂器，其他都寫得十分節制，而是配合樂隊整體的音響表現廟會的歡快景象。嗩吶在這首樂曲中扮演極為重要的角色，如何掌握北管風格演奏方法是關鍵。
    </p>
    <p><strong>3.</strong><strong>西樂中奏，經典學習</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在國樂發展的初期，「西樂中奏」曾經流行一時，基於當時的演奏水準與移植手法的局限性，無法達到預期成效。經過了這些年的努力，演奏水準大幅提升，特別是青年演奏家們都接受了嚴格的中西音樂訓練，在聲響的組合方面，已經能夠達到一定程度的平衡與融合，樂團也不斷地在找尋屬於自己的聲音。橫跨中西音樂的前TCO團長陳澄雄，默默地花了一年的時間，對於Rimsky-Korsakov《天方夜譚》進行移植的工作。他選擇竹青來演奏，主要的原因是樂曲的困難度，音響組合、演奏法的調整，不僅需要一流的演奏水準，更需要時間來慢慢磨練。
    </p>
    <p><strong>二、國人作品專場發表會</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019國人作品專場發表會，在團體方面有：
      NCO、高市國、台灣揚琴協會、國樂學會；個人方面有：楊杰儒、李欣芸等。由於高市國及國樂學會在前面已有介紹，不再贅述，以下就各場發表會提出觀察。
    </p>
    <p><strong>（一）臺灣國樂團作品發表會</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      臺灣國樂團在9月13日演出的「花漾寶島」音樂會，是一場以寶島特色花卉為主題的音樂會，整場音樂會共有五首樂曲，都是首次公開演出的曲目。上半場三首樂曲為盧亮輝《菅芒花幻想曲》、王乙聿《繁花》組曲與李哲藝《木棉花開》，並被收錄於《花漾寶島》專輯中。下半場第一首樂曲為王乙聿作曲、路寒袖作詞的全新創作《臺灣花》，長度8分半鐘，由女高音蔣啟真以臺語美聲唱法演唱。最後一首《花漾寶島》亦是這場音樂會的標題，曲長12分鐘，使用了許多音樂素材，包括《百合花的禮讚》、《六月茉莉》、《雨夜花》、《夜來香》、《桃花開》等樂曲，作曲家李佳盈巧妙地運用了原住民、臺灣歌謠、客家的元素融合在樂曲中，更象徵了臺灣多元文化的融合性。臺灣國樂團秉持以「國樂訴說臺灣最美的故事」為宗旨，都會以臺灣不同的景物為主題來策劃節目，也是該團年度音樂會的一個亮點。
    </p>
    <p><strong>（二）台灣揚琴發展協會作品發表會</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      台灣揚琴發展協會自2012年擴大成立以來，為了充實臺灣音樂比賽揚琴獨奏項目指定曲的國人作品數量，於2016年舉辦了一場10首國人創作發表會，並於隔年出版樂譜。2019年揚琴協會再次以邀約及徵曲方式獲得不少作品，並舉辦3場演出，發表了6首邀約及6首徵曲稿件，共計12首。這次的主題就是獨奏曲，並且以音樂比賽曲目為目標，因此，需要考量樂曲長度及演奏的難易度。參與發表的國人作曲家有羅正楎、王韻雅、蘇文慶、林心蘋、杜宛霖、何立仁等，期待在音樂比賽時，能夠成為常用的指定曲目。
    </p>
    <p><strong>（三）楊杰儒作品發表會</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      在2018年9月7日楊杰儒舉辦了第一次的創作發表「柳琴60快樂!!」後，短短一年再度推出「舞！楊杰儒創作發表會」，除了需要寫作能量外，更需要有對國樂的深度熱情。她學習柳琴與中阮將近20年頭，對於作曲也非常有興趣。在她演奏的歷程中，深感臺灣柳琴作品的缺乏，因而決定投入創作的行列，共同為這塊園地付上一己之力。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這場音樂會以「我的四季」套曲－春、夏、秋、冬為主軸，曲名分別為《舞春光》、《夏月舞》、《舞秋風》、《晴舞冬》四首樂曲為主，並以四個小故事來引導聽眾，也使用不同的配器來呈現，更能顯現出不同的四季風貌（楊杰儒，2019）。
    </p>
    <p>
      楊杰儒的創作曲調動人，音樂富有畫面，曲長約在3分半到7分半鐘之間，對於柳琴與中阮的推廣運用會有一定的果效。
    </p>
    <p><strong>（四）李欣芸作品發表會</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      「國樂作品流行化」是國樂走出同溫層，邁向群眾的方法之一。TCO在年度節目規劃時，除了維持一貫的經典國樂節目製作為主體外，還依不同的觀眾區隔開發新節目。在2019的傳統藝術季中，邀請了臺灣唯一獲得金馬獎與金曲獎雙金肯定的作曲家李欣芸，為TCO製作一場名為「有閒來坐－臺北城的十張椅子」。這是一場結合音樂、影像、吟唱等多重形式相互交融的演出。整場音樂會以10首作品來描繪作曲家以臺北為主題的音樂故事，從悠哉閒適的南港後山茶園、富有歷史價值的淡水馬偕的家&hellip;&hellip;到時尚繁榮的東區等，李欣芸用音樂呈現她對臺北的觀察與想像。她在創作時，既使用了國樂樂器的語彙，也融入現代流行、電子合成器等，讓樂曲不僅保有傳統韻味，也有現代流行時尚感。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      此次的演出搭配燈光以及黑白舊照片的放映，樂曲旋律悠閒輕快，沒有太大的跌宕起伏，但很是愜意，不僅令人輕鬆的瀏覽整個大臺北，也藉此讓觀眾看見這塊土地上美好的人事物。
    </p>
    
    <h3><strong>參、值得討論的趨勢與議題</strong></h3>
    <p><strong>一、</strong><strong>2018</strong><strong>年趨勢的持續追蹤觀察</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;要掌握趨勢的脈動，需要藉由長期持續觀察。本文回顧2018年提出的「多元跨域製作趨勢」及「國樂音樂特質的重要性」兩方面進行持續的追蹤觀察。首先，「多元跨域的製作趨勢」雖然方興未艾，但也僅限於TCO、NCO兩大職業樂團，其主要原因還是要有充裕的經費和樂團年度計畫的考量。以每年僅有1至2場演出活動的團隊而言，經典國樂作品的演出是他們的首選。即便是職業樂團的桃市國、南市國，都沒有實質的跨域，有的僅是配合影片。高市國9月與高雄兩大歌仔戲團（春美歌劇團、明華園天字歌仔戲劇團）合作「歌仔天王尬高市國－戲弦曲」，也是整年度10場演出中的唯一一場。整體而言，經典國樂的演出還是主流，至於票房的問題，因素過多，反倒是一年演出場次極少的民營團隊，可以開出亮麗的票房。如：先嗇宮民族管弦樂團在國家音樂廳的演出，四樓全開還能開出完售的紅盤；竹青以高超的演奏水準，獲選為新竹市的傑出演藝團隊，在地長期經營並建立了良好口碑，因此在票房上都能有極佳的斬獲。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;其次，「國樂音樂特質的重要性」方面，從筆者與作曲家們的言談之間，可以發現多數的作曲家已經關注這一個議題，也在自己的創作上努力嘗試，相信不久的將來，會成為臺灣創作作品的核心價值。
    </p>
    <p><strong>二、</strong><strong>2019</strong><strong>觀察的議題</strong></p>
    <p><strong>（一）教育成效彰顯，國樂人才輸出</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      臺灣的國樂教育重視學生學習的均衡發展，同時給予良好的品德教導，因此很受各地樂團的喜愛。這些年來，被新加坡、澳門、香港、蘇州、上海等地的樂團所招聘的人數不少，甚至還有被禮聘擔任職業樂團、學校社團指揮工作的，也不在少數，這是個可喜的現象，也解決了臺灣職業樂團團員飽和的困境。
    </p>
    <p><strong>（二）「國樂樂譜借用平台」成效彰顯</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      國樂學會為了服務學校社團合法取得音樂比賽曲目的授權，於2016年成立了「國樂樂譜借用平台」，透過學會與作曲家之間的多次開會協調，擬訂出了一個多方都能接受的借譜費用。經過了4年來的努力，成效已經彰顯，2019年一共借出249首，國人的作品有229首；非國人的作品有20首。未來持續和新的作曲家簽約，期盼能協助學校團體及非職業樂團合法取得樂曲授權。
    </p>
    
    <h3>結語</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年國樂的創作可說是豐收的一年，無論是從作品的數量、寫作手法的多樣、表現形式的多元，都為當下留下美好的記憶！雖說國樂的發展在作品，但是沒有藉由演奏的載體給予展現，它的存在也是沒有意義的。更重要的是，這些作曲家們辛苦孵出的作品，更需要觀眾來欣賞及肯定。在網路盛行的今日，地球已經變成扁平，距離已不是距離，當5G來臨時，在世界各地同步演奏的可行性已經成為可能，對於所有表演藝術的衝擊，更是要即早因應。面對來自多方面的挑戰，期盼2020國樂的園地能夠持續發光、發熱，開始嶄新的一頁！
    </p>
    <p class="bkt"><strong class="bkt">後記：</strong></p>
    <p class="bkt">
      &nbsp;&nbsp;&nbsp;&nbsp;能夠完成這次年鑑國樂類的觀察，除了感謝各單位提供的寶貴資料外，也要謝謝許多作曲家們願意無私的分享創作的理念與手法。最重要的是，要感謝林青儀、萬智懿、吳沛諭及蔡佩娟四位助理的協助。沒有妳們全年全心投入與詳實紀錄蹲點場次，這篇報告是無法完成的。在此，向妳們致上誠摯的謝意，讓我們在新的一年持續努力！
    </p>
    
