    <p>b.2019 NCO器樂大賽笛子新秀選拔「玉笛飛聲」</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;NCO長期關注本土人才培養，從第一屆嗩吶選秀至今，已陸續舉辦6種器樂選秀。本次笛子選秀亦吸引許多好手報名，初選共有26人投件，從中選出5人進入決選，但決選當天有一名參賽者因故棄權，因此實際只有4名選手參與比賽。
