    <p><strong>（一）研究</strong></p>
    <p><strong>1.</strong><strong>期刊論文</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      李美燕（2019a）發表〈東漢神獸鏡中的「伯牙彈琴」圖初探〉一文，透過對東漢神獸鏡中的「伯牙彈琴」圖像之研究，探討古琴在東漢時與道教信仰之關係，並以圖像中古琴形制與出土、傳世古琴作異同比較，探索東漢時期古琴可能存在之類型。李美燕（2019b）發表〈《天聞閣琴譜．二十四琴品》研究〉一文，探討〈二十四琴品〉與司空圖《二十四詩品》之關聯，並與二十四《琴況》略做比較，以提出對〈二十四琴品〉之評價與定位。此兩篇都是有關古琴音樂之研究，也是研究者長期關注的面向與議題。
    </p>
    <p><strong>2.</strong><strong>研討會論文</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由臺灣藝術大學中國音樂學系和中國音樂研究學會聯合主辦的「歷史與田野：中國音樂研究學術研討會」，於2019年9月21日在臺灣藝術大學國際會議廳舉行，總計有沈冬、呂鈺秀、施德玉、張儷瓊、莊文達、李曉天、馮長春、鄒彥、劉富琳、馬玉峰、金橋、卓頤、蔡秉衡等人在會議上發表13篇論文。其中有多篇關於國樂相關的議題，包括：張儷瓊（2019）〈【玉連環】四家箏譜研究〉，該文對於客家、潮州、十三音及北管四首箏譜，從5個面向進行比較分析，提出其精闢的見解；馮長春（2019）〈作為效果史的音樂理解－以《二泉映月》的詮釋為例〉，該文以「視域融合」、「效果史」等詮釋學理論對於阿炳的《二泉映月》提出其個人新的詮釋見解；金橋（2019）〈石破天驚
      二弦傳奇－&ldquo;勞弗檔案&rdquo;中歷史錄音《上海胡琴》之探析〉，該文以&ldquo;伯特霍爾德．勞弗檔案&rdquo;中於1901年記錄的一首胡琴錄音，運用樂曲及演奏技法分析，對該首樂曲的歷史定位與價值，提出概括與總結。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
