    <p><strong>1.</strong><strong>文字出版（樂刊、雜誌、專刊）</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年國樂領域的出版形式，主要分為文字與有聲兩大類。其中文字部分包括專書、樂訊、音樂活動回顧與評論；有聲出版以樂團和個人發行的專輯為主。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      2019年國樂領域的的文字出版一如繼往的有樂刊、雜誌類外，尚有一本專書出版。樂刊、雜誌：北市國《新絲路》、國立傳統藝術中心《傳藝》雙月刊、國家表演藝術中心《PAR
      表演藝術》、國家文化藝術基金會「表演藝術評論台」網站及《MUZIK》古典樂刊。除了《MUZIK》古典樂刊為紙本，《傳藝》雙月刊以紙本出版，並以電子檔在《傳藝Online》提供線上閱讀；《PAR
      表演藝術》也是以紙本與電子版線上兩種閱讀方式外，其他皆為電子版線上閱讀，國樂活動內容著重於樂訊披露及樂評發表。
    </p>
    <p>（1）《新絲路》雜誌</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;《新絲路》雜誌是臺北市立國樂團出版的線上雜誌，發佈於北市國官方網站與國際雜誌網路平臺「ISSUU」。2019年從第63-68期，共有6期。內容含括7大主題：新思路、封面故事、客席主筆時間、追光人物、TCO藝文馬克、TCO音樂實驗室、這些年那些事。內容以北市國的演出樂訊與活動的披露、音樂人物素描、跨領域的表演或新興樂團介紹、溫故知新的世界音樂及國樂發展的軌跡典故。如：鄭立彬（2019）〈傳統是基底孕育當代新創意〉一文中，述說了臺北市傳統藝術季節目製作的重點，以<span class="bkt">「傳統為經，創意為緯」</span>的理念，建構當代國樂的多元風貌；陳蓉（2019）〈向經典致敬綻放新世代榮光〉一文中，呈述了團長鄭立彬對於北市國40週年慶所規劃系列音樂會的報導，及對北市國未來發展的期許；江賜良（2019）〈淺析北市國的現代&ldquo;國樂&rdquo;聲響美學〉一文，作者以自身為馬來西亞的作曲家觀點，論述了團長鄭立彬對於樂團音響的要求與背後的聲響美學。
    </p>
    <p>（2）《傳藝》雙月刊、《傳藝Online》</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年國立傳統藝術中心出版《傳藝》雙月刊雜誌共6期（122-127），含括音樂、戲曲、舞蹈、工藝、民俗、評論6大面向。內容設計分為：主題故事、文化觀察、焦點人物、藝文訊息、傳．LIFE、傳藝家族、影音專區等7個部份。每期都有關於國樂方面的報導，共計有18篇，尤其是8月份的第125期，邀請了多位專家學者從多面向來探討國樂，如：陳鄭港（2019）〈轉折、轉變與遞嬗：華人漢系音樂現代化發展〉一文，爬梳新世紀華人漢系音樂現代化發展與生態；李秋玫（2019）〈耕耘在地化在華人世界遍地開花專訪臺藝大中國音樂學系榮譽教授林昱廷〉一文，呈述了國樂學會理事長林昱廷對臺灣國樂發展現況的剖析；施德玉（2019）〈「根」若穩穩地在甚麼都不是問題從臺灣國樂高等教育談起〉一文，剖析了兩岸培育國樂人才的特色與異同，強調課程安排之重要性；葉娟礽（2019）〈箏樂大師－梁在平的古韻新生〉一文，對於箏樂大師梁在平生平及對臺灣箏樂發展的貢獻做了詳實的報導；張儷瓊（2019）〈多元文化視域下的《2018臺灣音樂年鑑》〉一文，呈述了2018年臺灣音樂的多元面貌及年鑑團隊共同努力的成果報告展現。《傳藝》雙月刊有關國樂報導的文章18篇，如表6。
