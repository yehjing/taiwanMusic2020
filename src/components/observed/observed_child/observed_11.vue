    <p><strong>2.</strong><strong>檢定</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      臺灣現有「中華民國國樂學會考級」、「黃鐘獎國樂考級」與「台灣社會音樂考級」等三個國樂器考級檢定單位。考級檢定提供一般學習國樂的人士，在學習過程中一個檢核的平臺，讓他們能夠藉由外部機制檢核的結果，了解自己學習的成效，以此循序漸進學習，達到自我訂定的目標。2019年國樂學會在全臺共舉辦25場次，報名人數998人，缺考人數11人，延考人數38人，不合格人數61人，合格人數888人，合格率為88.98%，不合格率為6.11%。
