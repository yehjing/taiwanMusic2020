    <p><strong>三、國際與兩岸交流</strong></p>
    <p><strong>（一）國際交流</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      2019年在國際交流方面共有25套節目，39場次的演出。其中以NCO與日本北之台雅樂會在臺灣戲曲中心聯合演出的「雅正之樂－日本雅樂的音樂風情I、II」、與韓國國立國樂院在韓國首爾聯合演出的「音樂相遇・聽見臺灣」，這兩場音樂會都有一同演出的曲目。以「雅正之樂」這場音樂會中的《越殿樂》，是由國樂團與雅樂團共同合演的曲目，由於在音律上國樂團目前使用442
      Hz，而雅樂團使用的是430
      Hz，如何能夠一起演出而不會有突兀的感覺，是一個需要省思的課題。透過作曲家王乙聿的編配，他以弦樂來補足雅樂團沒有弦樂的聲部，作為音響的基底，並以不諧和的和弦呈現，破壞聽覺的音高感，讓演出的結果獲得大家的肯定，是跨文化交流成功的實例。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      其次，NCO於9月底至10月初，赴美參與密西根人文協會於底特律舉辦之「臺灣飲食文化節」中，演出「寶島辦桌」及「璀璨臺灣」兩檔音樂會。兩檔節目都獲得觀眾熱烈地迴響，約有1,700人觀賞。「寶島辦桌」節目，除了與主題－「臺灣飲食文化節」非常貼切外，四位歌仔戲演員的合作演出，讓國樂、戲劇、歌仔戲巧妙結合，將臺灣民間飲食文化以劇場音樂形式展現，獲得在場觀眾極大的迴響與好評，成功完成文化的交流。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      在民營樂團中，小巨人絲竹樂團的國際交流活動較為頻繁。他們到美國、加拿大與墨西哥等國演出，共計演出5場次。在加拿大溫哥華Western
      Front音樂廳與Sound of Dragon Ensemble聯合演出，也是該團近年來延續在加拿大的演出活動。
