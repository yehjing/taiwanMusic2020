    <p><strong>二、推廣</strong></p>
    <p><strong>（一）推廣音樂會</strong></p>
    <p><strong>1.</strong><strong>育藝深遠－藝術教育啟蒙</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      臺北市立國樂團除了本團擔任推廣音樂會的演出外，為了讓更多樂友能夠有一個合奏排練演出的團體，依不同年齡及專業差異，設立了市民國樂團、教師國樂團、學院國樂團、青年國樂團、青少年國樂團、少年國樂團等五個國樂團；同時為了演出的需要，設立了合唱團。這些團隊組成龐大的TCO家族，除了正式大型音樂會的需求外，也分擔了TCO展演、推廣及交流的任務。2019年完成了13場展演，也擔任36場「文化就在巷子裡」的推廣音樂會活動。TCO在6月份演出2場「國樂好好玩－TCO親子音樂會」，同時配合九年一貫教育「人文與藝術」課程的規劃演出「育藝深遠－藝術教育啟蒙方案」30場。
    </p>
    <p><strong>2.</strong><strong>國樂頻道－藝術教育普及化</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣國樂團以「國樂頻道」為題，設計了《聽音樂說故事》、&nbsp;《動物與我》、《偶像與我》、《光與我》、《知性淡水》、《萬花筒》等6套曲目，在樂團的排練場所，以演出、導聆及互動方式呈現。以《聽音樂說故事》為例，節目設計以一位小學四年級活潑好動的小朋友莉莉，和班上同學去參觀動物園所發生的一連串故事為核心。藉由《絕對不要搔老虎的癢》、《老虎磨牙》、《兩隻老虎》、《鴨子》等標題性明確的樂曲，並穿插著主持人的導聆，達到聽音樂說故事的目的（臺灣國樂團，2019）。NCO在推廣音樂會的策略上，所設定的觀眾群較為廣闊，不僅限於中小學生，也因此，在節目設計上更加多元。全年推廣音樂會演出80場次，參與觀眾人數約5,033人。
    </p>
    <p><strong>3.</strong><strong>校園巡迴－推廣國樂，服務偏鄉</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃市國的推廣音樂會是採用樂團進入校園演出的方式，和TCO的「育藝深遠」、NCO的「國樂頻道」將觀眾帶入表演場地的方式不同。桃市國為了服務在地的國小，尤其是偏鄉、資源較不足的學校，讓學子們能夠有機會近距離接觸國樂樂器，並欣賞優美的樂音。將樂團分為A、B兩組，以通俗、趣味性高的曲目為主，帶給學子們一個知性與感性之旅。
    </p>
    <p><strong>（二）比賽與檢定</strong></p>
    <p><strong>1.</strong><strong>比賽：</strong></p>
    <p>（1）全國學生音樂比賽</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;全國學生音樂比賽共分為團體、個人兩大類，參賽人員須為在籍之學生。共分為國小、國中、高中職以及大專組，其次再分為A、B兩組：A組為就讀音樂班、科、系、所者，B組則為就讀非音樂班、科、系、所者。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;107學年度全國學生音樂比賽團體項目中，屬於國樂類別的有：國樂合奏、絲竹室內樂合奏；個人項目中，屬於國樂類別的則有：揚琴獨奏、箏獨奏、柳葉琴獨奏、琵琶獨奏、阮咸獨奏。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國樂合奏各組自選曲概況，總計159首，其中國人創作有46首，占28.9%；非國人創作有113首，占71.1%。雖然仍大量選用非國人創作樂曲，但選擇臺灣作曲家作品的學校也逐漸增多。以臺灣作曲家而言，出現最多次的是蘇文慶的《噶瑪蘭幻想曲》，共計6次；而蘇氏的《風獅爺傳奇》、《臺灣追想曲》、《樂響》等其他作品，總共也出現了10次。特別在大專組中，大量選用國人作品，而且都有不錯的表現，是非常可喜的現象。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;非國人作品中，選用次數最多的是姜瑩的《絲綢之路》，共10次；其次是王丹紅的《太陽頌》，共9次。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;107學年度全國音樂比賽絲竹室內樂合奏自選曲，共計有210首，其中國人創作有86首，占41.0%；非國人創作有121首，占57.6%；傳統曲目有3首，占1.4%。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;絲竹室內樂合奏方面，選用國人作品的比例大幅提升，可見臺灣絲竹室內樂作品的推廣已逐漸取得成效。其中仍然以蘇文慶的《噶瑪蘭幻想曲》次數最多，共8次。作曲家張永欽的《雨》、《阿里山的日出》亦有許多學校選擇，共出現13次。在這一類組中，大專組選用國人作品的學校同樣佔大多數，相當具有指摽性的意義。
    </p>
    <p>另外值得一提的是，此次被選用的國人作品中，不乏新生代作曲家的作品如：徐瑋廷的《Re
      La》、鄭光智的《Naomi變奏曲》、劉至軒的《水雲間》等。其中鄭光智的《Naomi變奏曲》是2018年12月剛剛於國樂學會主辦的「2018國樂創作聯合發表會」中發表的新樂曲，選用這首樂曲的金門高中能在這麼短的時間內就將樂曲練熟，並取得特優的佳績，值得稱許。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在非國人作品方面，選曲呈現較為集中的狀況，以李博禪的《茶馬》最多，共計25次。其次是王丹紅的《沙漠玫瑰》，計12次。而深受大家喜愛，較為「老牌」的絲竹作品如楊春林的《美麗的鳳尾竹》、王愛康的《竹歌》，亦各有10次、7次的演奏次數。
    </p>
