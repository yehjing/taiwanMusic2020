    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      國立臺灣藝術大學中國音樂學系赴韓國的演出交流；台灣揚琴樂團赴馬來西亞的3場巡迴演出；隨心所欲樂團赴德國巡演5場；三個人赴日本與新加坡各1場的演出，都獲得各方極大的迴響。
    </p>
    <p><strong>（二）兩岸交流</strong></p>
