    <p><strong>3.「繁花盛開」桃園國樂節</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃園國樂節籌辦至今已邁入第7年，今年國樂節以「繁花盛開」命名，於8月3日至8月11日舉行，共計2場國樂工作坊、1場國樂研習營及5場音樂會（如表3）。適逢《梁山伯與祝英台》60周年，特別邀請知名小提琴家曾宇謙擔任協奏。「行旅寶島」音樂會邀請臺灣國樂團演出；「奏響」音樂會由指揮家瞿春泉領銜桃園市國樂團，安排了多首協奏作品。「城市之聲」音樂會為達到交流與推廣之目的，邀請日本鶴翔二胡樂團一同演出，由林亦輝指揮；另外，則是由林昱廷指揮帶領參與國樂研習之學員與桃園市國樂團聯合演出。「璀璨」閉幕音樂會邀請指揮家黃光佑，並特別委託2首新作品，分別為劉至軒的《桃源》及吳宗憲的《夢之隨想》。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年桃園國樂節的節目內容精彩豐富，展現出國樂多元的樣貌。桃市國更是透過國樂節不同的音樂活動，為桃園增添更多藝文氣息，活絡桃園的國樂發展。
