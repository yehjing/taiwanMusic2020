    <p class="firstRow"></p>
    <h3>前言</h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;創作是國樂發展的基石，也是音樂產業鏈裡，上游端提供給演奏者所需要的新養分與新的刺激，它也建構、呈現了當代、在地的音樂文化。作曲家藉由對當代人事地物的觀察與體驗，透過情感的內化，將音樂化成樂譜，讓演奏家能夠為當代音樂留下音響紀錄，也讓國樂的發展得以延續。2019年的國樂創作數量共有103首，編創有9首，相較2018年創作72首，編創12首，有了大幅的增長，由此可見國樂創作在持續蓬勃發展的現象。以下將從國樂領域的生態與發展及國人創作與發表兩面向來分析與評述，茲分述如下：
    </p>
    <h3>壹、國樂領域的生態與發展</h3>
    <p><strong>一、展演活動</strong></p>
    <p><strong>（一）公部門樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣以公部門經費成立的國樂團有職業與非職業兩類，職業樂團有以公務機關及基金會兩種不同營運的模式；非職業樂團則由客委會、市公所、文化中心等不同機關所支持成立。
    </p>
    <p><strong>1.</strong><strong>職業樂團</strong></p>
    <p>（1）臺北市立國樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣的五大職業樂團的演出是國樂展演活動的主要核心，其中又以成立最久、資源最豐富的臺北市立國樂團（以下簡稱TCO）的年度展演活動最具規模。2019年適逢該團40週年，TCO的樂季全年規劃46項55場演出，由樂團直接參與演出的有25項34場；屬於跨界性質的佔10項16場，比2018年的11項17場稍有減少。全年包含附設團隊及傳統藝術季外製節目等，全年共計有137場次，服務觀眾人數83,807人次。年度歲入合計16,170,164元，其中票房收入為14,036,000元，創歷年新高，成效卓著（臺北市立國樂團，2019）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年委託創作27首，國人創作23首，另有1首移植作品，相較2018年的20首委創作品，17首由國人創作有明顯增加的趨勢。據鄭立彬團長表示，他每年都會邀請3位作曲家為3個附設團隊創作新曲，同時希望作曲家能夠考量樂曲的難易度，以提供給音樂比賽使用的方向來創作，藉此為一般團隊累積合宜的曲目。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上半年度，TCO繼2017年首創臺灣歌劇「李天祿的四個女人」後，今年再次推出「我的媽媽欠栽培」，音樂融合了歌仔戲、流行樂、恰恰、歌劇、越南歌謠等不同風格的元素。表演形式則結合歌劇、音樂劇、歌仔戲、合唱團、布袋戲、人偶戲等，多元感官的刺激，是一齣雅俗共賞、老少咸宜的精彩製作。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;下半年適逢TCO創團40週年，在9月份推出TCO40週年特展、音樂論壇、發行專刊等深具教育與學術內涵的團慶活動，並將活動主題環繞「名家慶團圓」的方向來擘劃。以「再現經典」、「團長」、「首席指揮精選」、「總監」、「國樂新勢力」、「市國之星」6個新系列，加上原有的「飛魔力」、「閃亮亮」、「藝享」及「小而美」系列，維持一貫的10個系列，作為團慶的主軸。在「市國之星」系列中，11月30日舉辦的「暢樂之夜」音樂會，邀請曾在北市國擔任指揮的李時銘、郭聯昌和陳中申三位先進，再度與樂團合作演出，共同見證樂團的發展與今日的成就。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;北市國40年來在歷任團長、指揮的努力經營下，奠定了良好的基礎，對於現代國樂的發展，具有重大的貢獻與影響。
    </p>
    <p>（2）臺灣國樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣國樂團（以下簡稱NCO）隸屬於文化部轄下的傳統藝術中心，是唯一隸屬於中央的國樂團。2019年的演出活動共計110場次，其中國內的展演活動有13場次（包含2場獨奏音樂會、1場弓弦樂的音樂會）；跨界合作的演出有6場次；應邀參與國樂節的演出有4場次；NCO大賽2場次；國外巡演5場次；推廣音樂會80場次；推廣講座9場次。委託創作的樂曲有12首，其中國人創作10首，非國人創作2首（臺灣國樂團，2019）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以經典國樂為主體演出的面向，今年有「閻選之樂．星光藝奕」、「藝號人物－盧亮輝」、「花漾寶島」、「璀璨臺灣」等4檔節目，5場次的演出。「越嶺～聆聽布農的音樂故事」則是作曲家吳睿然為國樂團與布農族的全新創作，是一場國樂團結合布農族原音，詮釋部落故事的音樂劇場。在跨界演出的面向，有3檔節目，和O劇團、國光劇團、掌中劇團等合作演出6場次。在跨文化演出的面向，有在國內與日本北之台雅樂會聯合演出的「雅正之樂－日本雅樂的音樂風情Ⅱ」；赴韓國與韓國國立國樂院創作樂團聯合演出「音樂相遇－聽見臺灣」。近年來，NCO除了經典國樂演出外，更開拓了跨文化、跨界的演出活動，明顯地呈現與其他樂團演出內容的差異化及自身的特色。樂團藉由與傳統劇種合作，能夠有機會吸取傳統的養分，但同時更要注意自身主體核心價值的堅持，讓樂團從跨文化、跨界所獲得的滋養，得以在經典音樂會演出中展露，才是真正的價值所在。
    </p>
    <p>（3）桃園市國樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃園市國樂團（以下簡稱桃市國）在2019年年度演出共舉辦19場次，其中專題音樂會9場、在地國小聯合音樂會舉辦1場、桃園市國樂節5場、桃園之星（弦樂、彈撥、吹打）3場及戶外演出。演出地點以桃園市為主，共有11場室內及3場戶外演出；臺北市有4場，包含在國家演奏廳的3場族群音樂會；高雄有1場，場地是在高雄市衛武營音樂廳（桃園市國樂團，2019）。從演出場地可知桃市國的企圖心，雖然成立才3年，擁有臺灣職業樂團平均年齡最年輕的優勢，充滿活力與熱情，不僅為桃園市注入豐沛的音樂能量，更成為桃園市的文化大使，達成城市交流的目的。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;桃市國今年的委託創作共有6首，全是國人創作。有2首是鋼琴協奏曲、2首是為族群音樂會而寫、2首是為桃園市國樂節閉幕音樂會而寫。其中李哲藝《桃園四季》與劉至軒《桃源．Taoyuan》兩首，都是以桃園為題的作品，這正是桃市國的策略。每年邀請作曲家以「桃園」為題，為桃市國量身創作樂曲，期盼在長期累積之下，能為該團留下豐沛的保留曲目，更能增加品牌的辨識度，建立與其他樂團之間的差異化。
    </p>
    <p>（4）臺南市民族管絃樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺南市民族管絃樂團（以下簡稱南市國）今年的大型音樂會主要分為「國樂正夯」、「菁音獨奏家系列」、「許石臺灣歌巡演」三個系列及獨立的2場音樂會，共計演出15場次。今年是「國樂正夯」臺南國樂節舉辦的第二屆，一共有5場音樂會；「菁音獨奏家系列」一共3場；今年是作曲家許石的百年冥誕，所以南市國特別舉辦了「許石臺灣歌巡演」紀念音樂會，共巡迴5場，主要演出許石創作的知名樂曲，如：思想起、丟丟銅、安平追想曲；2場音樂會為台江文化中心「試營運」開幕音樂會與國立中正大學三十週年校慶音樂會「鍾情中正」（臺南市民族管絃樂團，2019）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;配合臺南市政府的公務演出有19場；在臺灣船園區所舉辦的「音樂棧」系列音樂會有11場；藝文推廣演出有10場；校園推廣有12場，共計52場。整體而言，全年度的演出份量不輕。也因此部分場次分為A、B兩組分擔演出的工作量，除了可以節省人力，也可增加效益。南市國由於團員人數僅20餘人，原本就比較適合做小型的演出。在規劃大型音樂會時，需要增加大量的臨時團員，除了需要經費外，團員的默契、樂團聲響的建構，都需要時間來磨合與建立。樂團本年度沒有任何委創作品，對於樂團建立自己的保留曲目，較為困難。
    </p>
    <p>（5）高雄市國樂團</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;高雄市國樂團（以下簡稱高市國）今年所舉辦的大型音樂會共計10場，其中在高雄春天藝術節演出3場；年度音樂會5場；附設青少年國樂團演出2場。全年度共委託創作10首作品，其中國人創作7首，非國人創作3首。在年初的首場音樂會「豬事大吉．四海迎春」發表了9首新創作品，作品以過年為主題創作樂曲，作曲家藉由不同面向的關注，讓音樂來述說不同族群的年節氣息。5月份與臺灣豫劇團的演出《龍袍》，是高市國結合在地資源，豐沛演出能量的一個途徑（高雄市國樂團，2019）。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;11月份是高市國專門為兒少們所舉辦的推廣音樂會，對象以高雄市的中小學為主，一共舉辦20場次。藉由大家耳熟能詳的卡通歌曲來成為教導兒少們認識國樂的媒介，進而達到推廣國樂的目的。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年臺灣的國樂展演活動在五大職業樂團依各自屬性與定位發展下，有了豐碩的成果。在演出的形式上，除了傳統國樂合奏、協奏外，有與日本跨文化的演出、有新創臺灣歌劇；有經典、有流行，多元面貌的展現，讓國樂拓展了不同觀眾群體，也讓國樂不再是守舊、古老的代名詞。在新創作品中，共有55首，其中國人創作有46首，國人創作比例達83.64%，是一個可喜的現象。各個樂團發揮各自的優勢，演出場次數量不是重點，而是如何建立樂團自有、不可取代的特色，才是重點。
    </p>
    <p><strong>2.</strong><strong>非職業樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由公部門支持成立的非職業樂團有，臺中市客委會成立的臺中市客委會國樂團；基隆市文化中心成立的基隆市國樂團；彰化市公所成立的彰化市立國樂團；苗栗縣文化觀光局成立的苗栗縣立國樂團等，每年都有定期的演出。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年臺中市客委會國樂團3月在臺中國家歌劇院中劇院、10月在臺中市葫蘆墩文化中心各演出一場。客委會國樂團是全臺少數以推廣客家文化為主體的樂團，透過音樂來展現客家的精神與文化風貌。在演出的曲目上，以客家音樂為主軸，國樂經典曲目為輔。團員目前大約60人，來自臺中、彰化與南投等地區，年齡自14至60餘歲，涵蓋老、中、青、少四代。有科班、有業餘；有學生、有社會人士。每逢舉辦音樂會時，都是由該團團員為主體，只有極少數需要補足的聲部尋求支援，此舉對於在地人才的培育及樂團的永續經營意義重大。
    </p>
    <p><strong>（二）民營樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣的民營樂團為數不少，也是國樂活動在民間活躍的主要動力來源。以下僅就小巨人絲竹樂團、新竹青年國樂團、台灣愛樂民族管絃樂團三團在2019年的展演活動提出觀察報導。
    </p>
    <p><strong>1.</strong><strong>小巨人絲竹樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;小巨人絲竹樂團為國藝會2019 TAIWAN TOP演藝團隊，2019年在臺北市舉辦「兩岸薪傳系列音樂會」2場；「絲竹室內樂系列」4場，及「演奏家系列」2場主要的展演活動。委託創作有3首，一首為笛協奏曲、一首為絲竹室內樂，一首則為三件國樂器與電子音樂的作品。為了服務鄉里，今年特別組成了10-15人的絲竹室內樂團，邀請安敬業擔任指揮，以「臺灣風情畫」為主題，選擇國樂經典曲目及大家耳熟能詳的民謠、流行歌曲等，並透過主持人的導聆，提供給民眾一個感性與知性的音樂饗宴。在基隆、新莊、南投、桃園及永和市等地演出，共計5場次，普遍受到觀眾的歡迎與肯定（小巨人絲竹樂團，2019）。
    </p>
    <p><strong>2.</strong><strong>新竹青年國樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;新竹青年國樂團（以下簡稱竹青）是具有極高演奏水準的民營樂團，2019年獲選為新竹市傑出演藝團隊。2019年竹青一共演出7場，除了2場經典音樂會外，另外2場屬於竹塹國樂節、2場兩岸交流音樂會、1場與港藝中樂團聯合演出（竹青，2019）。除了年度展演外，樂團長期承辦新竹市的「竹塹國樂節」，由於成效卓著，引起了各地的仿效，產生「國樂節」效應。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;竹青由來自臺灣各大專院校菁英所組成，擁有優異的演奏技巧。雖然參與樂團排練、演出都沒有任何費用，但是因著團員素質整齊，凝聚力高，以致團員的流動率低，具有絕佳的默契，演出水準與世界各地職業樂團相比，絕不遜色。名指揮家陳澄雄在6月9日與該團合作首演Rimsky-Korsakov的《天方夜譚》時，在演奏會現場表示，這首作品具有相當難度，需較長的時間排練，職業樂團會因為需要長時間排練而不合適；業餘樂團會因為演奏水準不夠而無法駕馭，目前應該只有新竹青年國樂團有能力擔綱。
    </p>
    <p><strong>3.</strong><strong>台灣愛樂民族管絃樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;台灣愛樂民族管絃樂團（以下簡稱台灣愛樂）受到陳啟川文教基金會與長安樂器股份有限公司的贊助，成為南臺灣的一個半職業樂團。創團至今已有8年。2019年的展演活動共有6場，其中大型音樂會有4場；室內樂2場。全年委託創作有2首，其中國人創作1首，由陳筱喬為歐陽孝宜創作的《孤花》（台灣愛樂，2019a）。台灣愛樂在粉絲專頁寫下的目標：<span class="bkt">「讓世界因國樂而認識美麗的寶島臺灣，讓臺灣愛樂從高雄出發、立足臺灣、邁向全世界。」</span>（台灣愛樂，2019b）
    </p>
    <p><strong>（三）個別樂器族群的表演生態</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在臺灣除了以國樂團的形制營運外，近年來以族群樂器成立的團體，逐年增加中。以古箏組成的團體有：大觀箏樂團、台北正心箏樂團、風雅頌古箏樂團、谷方當代箏界等約22個單位；以彈撥樂器為主體的有：台北柳琴室內樂團、琵琶雅集、台中琵琶樂團等；以揚琴為主體的有：台灣揚琴樂團、蝴蝶琴室揚琴樂團；以打擊樂為主體的有：天鼓擊樂團、紘采樂集等；還有以笙、胡琴或者不同樂器組合的形式，形制多元且有優質的演出表現，為臺灣的展演活動增添了豐富的色彩。
    </p>
    <p><strong>1.</strong><strong>風雅頌古箏樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;風雅頌古箏樂團是桃園市的傑出演藝團隊，由古箏演奏家徐惠綺成立。2019年的展演活動有4檔節目5場次的演出；兩岸交流活動有2檔節目5場次的演出；推廣音樂會有5項活動6場次的演出（風雅頌古箏樂團，2019）。演出的地點不僅限於桃園市，還前往臺北市、基隆市、新竹市、臺中市等地，是一個非常活躍的演出團體。
    </p>
    <p><strong>2.</strong><strong>台灣揚琴樂團</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;台灣揚琴樂團是臺中市的傑出演藝團隊，由揚琴演奏家鍾泉波成立。2019年的展演活動有10檔節目14場次的演出；8月份前往馬來西來巡迴演出3場次；前往金門、澎湖、臺中、雲林、連江及新北市進行校園巡迴推廣音樂會，共計演出25場次。今年委創有5首，國人創作2首；編曲/配器2首，國人1首，都在「花現琴韻－台灣揚琴合奏作品之夜」音樂會中演出（台灣揚琴樂團，2019）。這些新創作樂曲，為台灣揚琴樂團創造出不同的樣貌，也讓觀眾一睹揚琴合奏編制在不同作品中的展現。
    </p>
