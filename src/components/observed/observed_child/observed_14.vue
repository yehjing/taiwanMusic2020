      <h2>2019年度跨界音樂與其他之活動觀察與評介 ─ 世界、跨界、新住民：臺灣社會環境的改變與新音樂形態的發展</h2>
      <span>蔡宗德</span>
    </div>
    <hr/>
    <p class="firstRow"></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;世界音樂、跨界音樂與新住民音樂早已經不是什麼新的音樂形態，但在以西方古典音樂、流行音樂，臺灣傳統音樂與現代國樂為核心的音樂文化環境中，世界音樂、跨界音樂與新住民音樂終究只能算是非主流文化。然而，隨著臺灣國際化的加深、多元文化社會的形成、東南亞婚姻與移工的引進以及政府的新南向政策的影響下，此三種音樂形態也慢慢受到政府、大學以及社會的關注與重視。然而，儘管這三種音樂形態與現象受到人們的關心，但是臺灣整體的社會與學校教育中對於多元社會該有的觀念培養、不同文化差異的尊重、民族文化認同的養成，甚至是對於鄰近國家社會文化體系的了解不深的情況下，更何況是對世界音樂、跨界音樂與新住民音樂這三種音樂形態與現象的喜愛欣賞、結構特性、文化內涵的了解，實際上尚有一條漫長的路要走。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;為了要了解世界音樂、跨界音樂與新住民音樂在2019年發展的概況，本文將分為「邁向蓬勃發展的世界音樂」、「多元藝術元素結合的跨界音樂」以及「強調身分認同的臺灣新住民音樂文化」等三節來探究2019年臺灣在世界音樂、跨界音樂與新住民音樂上的發展特性，並針對各個單位、團體與個人在此三樂種上的努力、發展過程中的問題與困難、以及面對這些音樂形態該有的觀念與態度，作一簡單的陳述。
    </p>
      <h3>壹、邁向蓬勃發展的世界音樂</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;隨著文化全球化的趨勢，在西方古典音樂、流行音樂、臺灣傳統音樂等音聲元素交織而成的多元音景中，世界音樂對於臺灣的音樂發展與觀眾的聆聽經驗，漸漸占有一席之地。然而，如此蔚為臺灣音樂景觀中不可抹滅的一環，卻在人們的書寫中以一種「小眾」的姿態顯現，更甚者則隻字未提，肇端乎臺灣觀眾聆聽經驗的侷限性，以及對於「世界音樂」的想像與未解，進而使得「世界音樂」似乎只能以一種學術名詞的存在而閉鎖於象牙塔中，而在各種官方的資料中也難見其影，往往僅以寥寥數筆帶過，「世界音樂」豐沛的展演能量及其作為幫助觀眾，藉以認識、了解他者文化的路徑，幾受遮掩、難以發聲。本節藉由蹲點觀察2019年與臺灣相關的世界音樂展演，除爬梳展演之本體現象外，更企圖透過「新南向政策的影響與文化藝術機關的作為」、「臺灣在地世界音樂團體發展」、「教育的深化與民間的交流活動」等三個思考路徑，分項陳述2019年世界音樂展演的現象、成果及其發展的概況，同時挖掘過往存在但卻被忽略或未被加以書寫的臺灣世界音樂展演的個人、團體－此一觀眾或樂界在關注邀請自國外的世界音樂展演而經常遺忘的「邊陲」，事實上，在臺灣文化多元的環境裡所產生的世界音樂展演能量相當豐沛。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在「世界音樂」展演的場域或民族音樂學研究的範疇中，音樂（音聲）經常與戲劇、舞蹈互為主體，其間的互文性結構嚴實，因此在觀察與書寫時很難切割，或是因其表面以戲劇或舞蹈作為主要呈現模式而將其遺漏，若在書寫上單純關注其音樂本體結構的呈現與分析，恐僅加深觀眾對於「世界音樂」想像與未解的刻板印象，而世界音樂作為他者文化的載體與認識路徑之功能性，便消失殆盡。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「新南向政策的影響與文化藝術機關的作為」一節，主要探討在蔡英文總統執政下，臺灣的中央各級或地方政府文化機關，不論出於政策與例行公務所需的制式舉行，或是真正帶領民眾敞開心胸認識、了解鄰居國家的文化，皆不能否認在「新南向」政策的影響下，發生於臺灣的世界音樂展演中，東南亞表演藝術變得更受到關注。「臺灣在地世界音樂團體發展」一節主要論及臺灣世界音樂展演團體的發展概況，這些團體未必成立於2019年，或未必從2019年才開始活躍，只是過去被視為「小眾」而較少受到著墨，儘管如此，然終不能因其為「小眾」而使其失聲。最後，儘管有展演活動，但若不透過教育或交流層面的執行並加開縱深，世界音樂終究不過是一種「獵奇」的工具或全球化下文化平權的口號而已。因此，在「教育的深化與民間的交流活動」一節，著重探究目前所見之教育或交流的現象與成果。</p>
      <h4>一、新南向政策的影響與政府文化藝術機關的作為</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2016年中始，「新南向」政策成為蔡英文總統重要的執政方針之一，不同於以往的策略布署，除與東南亞、南亞、紐澳等地區加強經貿來往外，同時也著重於文化、藝術、教育等軟實力層面的交流，在此三區塊的預算更較以往充足，藉由政府的經費支撐，官方或民間皆更有能力推動臺灣與東南亞文化之交流，因此在世界音樂展演的部分，其中東南亞元素也躬逢其盛地在此熱潮上發展，不論是臺灣個人、團體赴東南亞交流展演，或是東南亞個人、團體赴臺灣交流展演，此一雙向流動的架構能夠得到加強。此處以中央各級主掌文化、藝術、教育等機關如文化部（部本部與文化資產局、國立傳統藝術中心、國立臺灣美術館所屬國立臺南生活美學館、國立臺灣博物館等）、國立故宮博物院（南部院區）為撰述重點，凝視並分析政府機關在「新南向」政策的框架下所作出的反應－不論是透過經費補助、個人或團體的邀演、文化藝術人才的培養、學術研究的雙向交流、輔導新移民的成果展示等等層面－而這些反應在臺灣的世界音樂音景中產生激盪與思考。由於此處談的是新南向，故僅著重於臺灣的東南亞音樂相關現象，並不涉及於其它地域文化。另外，在下列的機關或場館亦可看出，執行或展演的場域並不限於表演藝術空間，以典藏、展示為主並且階級化、品味化、精英化、去脈絡化（Bourdieu,
      1979）的「白盒子（white cube）」空間－美術館、博物館，也已成為執行或展演世界音樂的場域。
    </p>
      <h4>（一）文化部</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;作為中央主管文化相關事務的最高機關，文化部本部應對新南向政策的策略相當多元，其中又以「東南亞事務諮詢委員會」以及「臺灣青年文化園丁隊」，最能夠反映「新南向」的政策與活動。東南亞事務諮詢委員會於2015年即首度籌組，現今為文化新南向的重要交流平臺，委員會成員涵蓋了來自東南亞與臺灣藝術領域的學者專家，今年度的東南亞事務諮詢委員會會議，其中一個討論議題「臺灣與南島地區音樂文化的對話與區域共構」由筆者籌組、規劃並主持，在此框架下，國立傳統藝術中心黃馨瑩科長報告臺灣音樂館業務、亞太樂器常設展、亞太傳統藝術節等業務面向，接著由國立臺南藝術大學民族音樂學研究所教授兼亞太音樂研究中心主任胡敏德教授（Prof.Dr. Made Mantle Hood）以「臺灣與東南亞各國間表演藝術合作平臺初探」為題，進行專題演講，前述二項皆由國立臺北藝術大學傳統音樂學系李秀琴教授回應。會中，除學者論壇外，亦邀請致力於世界音樂跨界展演的臺灣團體日央人兒<em>Matahari</em>演出，將學術與政策的層面以及音樂展演的實踐整合為一。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化部另一與此相關的重要作為，即是推動「臺灣青年文化園丁隊」計畫，選拔臺灣優秀的青年學子赴東南亞、南亞、紐澳等地區交流學習，今年度共有兩個獲選隊伍與世界音樂研究、展演相關的隊伍，國立臺南藝術大學「『爪哇伯公』表演藝術交流－漫步日惹鄉村傳唱臺灣民間故事」與國立臺北藝術大學「韶聲越南才子樂團尋根計畫」，分別前往印尼、越南進行田野調查，並與當地藝術家交流，同時策劃實際展演，在國內外皆有亮眼的表現。另外，若再加上未入選的「『掌中臺印』：以布袋戲串起臺灣與印尼的文化共感」隊伍，今年度在世界音樂範圍中的投選隊伍共有三隊，較往年多，由此亦可見，文化部「臺灣青年文化園丁隊」計畫，也是讓臺灣觀眾有機會接觸到世界音樂展演的可行途徑之一。</p>
      <h4>（二）文化部文化資產局</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以有形文化資產見長、在無形文化資產方面亦戮力投入研究的文化資產局，不僅關注臺灣的文化資產保存，近年來在政府「新南向」政策的大方針下，亦積極推動與東南亞文化資產之間的交流，並策劃「看見印尼．守護臺灣」（2017）、「洞里薩湖 樂舞柬影」（2018）、「驚艷越南」（2019，圖1）等無形文化資產國際交流展演活動。表演藝術是無形文化資產中重要的一環，文化資產局在推動臺灣與東南亞無形文化資產交流的過程中，將東南亞地區的世界音樂展演形態，以官方的位置並系統性地帶入臺灣。今年度以越南作為主軸，展覽同時亦搭配實際展演，邀請昇龍水偶之舞劇團（Đoàn
