      <strong>「異國風華・臺灣加油</strong>
      <strong>－</strong>
      <strong>2019</strong>
      <strong>新住民歌唱比賽」</strong>
      <strong></strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由高雄市政府教育局與臺灣中油大林煉油廠所主辦的新住民歌唱比賽，至2019年已舉辦至第三屆，參與的群眾除有來自全省各地的新住民家庭外，國籍也趨於多元。事實上，中油大林煉油廠時常舉辦各種大小歌唱與舞蹈比賽，對象包含員工、區域居民或是在地新住民，而筆者則單一以新住民演唱臺灣歌曲，或親子演唱自身的母語組作為觀察對象。新住民組別比賽項目使用大會提供伴唱系統之國臺語歌曲，前兩屆的第一名皆為越南籍阮桂枝女士，今年榮獲第一名為印尼籍何依宓女士，以臺語歌《攏細為著你啦》作為比賽歌曲，而親子組冠軍則為印尼籍母親謝美麗與女兒蔡羽純共同演唱《甜蜜蜜》的印尼語原版
      <em>Dayang</em>
      <em></em>
      <em>Sanpan</em>
      。
    </p>
    <p>
      <strong>四、國立臺灣博物館「東南亞樂舞表演」</strong>
      <strong></strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國立臺灣博物館於5月18日國際博物館日連同創館111周年，規劃系列慶祝活動主題「臺博館，擁抱未來式」，邀請民眾以多元、創新的角度認識臺博館的百年歷史、建築與藏品。系列活動邀請駐館的印尼「竹韻揚聲樂團」以及「泰友印越舞蹈社」（VIT
      Dance
      Group）表演，展現臺博館多元文化的特色。印尼竹韻揚聲樂團由來自印尼的新住民所組成，為國立臺灣博物館駐館表演團體。以西爪哇的傳統樂器印尼安格隆搖竹為主軸，有固定排練時間，是北部著名的印尼安格隆搖竹樂團。泰友印越舞蹈社是於2015年10月10日在臺北市政府國慶表演後而在同日正式成立的舞蹈團，團員為來自不同國家的新住民，包括泰國、印尼、越南、菲律賓以及新二代。秉持多元文化的精神，互相學習舞蹈與瞭解不同文化，曾受邀在2016年蔡英文總統就職典禮上表演。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;活動由兩位新二代女性擔任主持人，整場以器樂及舞蹈輪流表演的模式進行。在印尼竹韻揚聲樂團方面，表演曲目有臺語歌《小姐請你給我愛》、國語歌《感恩的心》與《月亮代表我的心》，以及印尼歌曲或民謠如<em>Ibu Kita Kartini</em>、<em>Ampar</em><em>A</em><em>mpar</em><em>P</em><em>isang</em>、<em>Tanah Airku</em>、<em>Bengawan Solo</em>（《梭羅河畔》）、<em>Dayung Sampan</em>（《甜蜜蜜》），這些都曲子都能勾連出臺印之間的共同音樂記憶。泰友印越舞蹈社則帶來泰國與菲律賓舞蹈表演，泰國舞蹈的曲名為《歡迎來到清萊》，菲律賓舞蹈的曲名為<em>S</em><em>alakot</em>。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國立臺灣博物館致力於培訓東南亞語系的導覽志工，這其中有許多不同國籍的新住民為駐館導覽員，藉由多元文化環境，讓不同文化互相交流，雖然各自表演不同的藝術類型，但音樂舞蹈團隊都由不同國籍的新住民一同參與，因此能夠在印尼安格隆搖竹樂團看到越南、泰國等等的新住民，而在泰友印越舞蹈社更是直接把這個概念使用在團名之上。</p>
    <p>
      <strong>五、「</strong>
      <strong>108</strong>
      <strong>年度港都風情異國文化藝起來</strong>
      <strong>－</strong>
      <strong>印尼文化嘉年華」</strong>
      <strong></strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由高雄市政府勞工局所承辦的「108年度港都風情異國文化藝起來」，為市政府團隊所主打的東南亞文化體驗系列活動。此活動主要由泗水臺商團體以教育與民間參與的概念籌劃，並在其中投入許多的資源，同時有長官蒞臨「觀摩」，所以也促使此文化嘉年華成為近幾年規模最大的一場，各個新住民團體、留學生團體以及移工團體都在此時聚集（圖6）。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從上述幾場較為大型的活動中可以觀察到，活動大部分是由地方非政府組織或民營組織為主導，政府機關則以補助的方式或是從旁協助的身分介入，較少有機關專員深入了解不同文化背景並作解析，而活動的評審或是委員，大都不瞭解當地音樂形態或文化背景，卻成為評斷這些演出高低的「老師」。例如，歌唱比賽中通常使用臺語或歌詞充滿「苦情女性」經驗的歌曲，比較能獲得較高的關注，而親子組裡使用新住民母語的歌曲，由於評審並不熟悉該國語言以及歌曲的特性，每每各國兒歌或民歌重複的旋律較為繁沓時，主辦單位通常都不曉得何時停下歌曲的伴唱；2017年高雄伊斯蘭開齋節本質是宗教性活動，高雄市政府勞工局的慶祝活動以高雄清真寺教長來進行祝福念誦作為開場，卻同時邀請「峇里島文化工作室」演出由印度教信仰系統延伸的傳統迎賓舞，由此可以看出臺灣普遍對於印尼宗教信仰體系以及社會文化背景的不了解，進而造成不適當的行為；2019一整年各種已幾近於氾濫的新住民歌唱比賽或舞蹈比賽，雖然不同國籍的母親或是新二代很積極地參加，在不斷的行為複製下逐漸成為常態，因而使得這些新住民表演藝術形態與文化本質往往遭受忽略。在新住民各種表演藝術形態中，舞蹈是最普遍存在的，表演者往往捕捉印象中的母國舞蹈記憶，再根據社群媒體平臺的影片資訊來重新編舞，跳舞表演服裝也經常是整場表演的重頭戲，華麗繽紛的東南亞傳統服飾總能吸引觀眾的目光，讓路過民眾駐足欣賞。這樣活動的優點在於能夠增加民眾參與度，但在不斷的曝光之後，多半流於形式化，一般大眾只能從這些「去文化脈絡」的服飾與流行歌曲中瞭解另一個國家的文化。同時，許多新住民母親並非專業舞者，所能使用的歌曲或舞蹈，通常只是離開母國前對於過去環境的有限認知，加上他們對於家鄉的想像而重新呈現給臺灣觀眾，這樣的表演藝術形態不可避免地與其母國文化有一定程度的差距，對於新住民與其下一代，甚至是對於一般想了解新住民母國文化的臺灣觀賞而言，其幫助則是十分有限。
    </p>
