      <h4>（五）國立臺灣博物館</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      國立臺灣博物館早在日治時期建館時即開啟了與東南亞表演藝術的機緣。1908年（明治四十一年）擔任總督府博物館（國立臺灣博物館前身）首任館長的植物學家川上瀧彌（1871-1915）於1911年（明治四十四年）到東南亞地區出差並田野調查將近一年，帶回許多表演藝術相關藝術品如戲偶（<em>wayangan</em>）、面具（<em>topeng</em>）等等（國立臺灣博物館，
      2009），這些藏品堆於該博物館至今百年，被歸為「無法解釋」門類。近年該館致力於與新住民的連結，並與學界結合，企圖為前述「無法解釋」門類梳理出清晰的脈絡，進而作為展覽或演出的研究基礎。在博物館與新住民的連結的這一區塊，該館透過辦理東南亞樂舞展演、移民日、印尼蠟染節、印尼國慶日等活動，將東南亞地區的藝術文化引介予臺灣觀眾，而在這些活動當中，包含印尼安格隆搖竹、印尼巨獅舞、爪哇宮廷舞、峇里天堂鳥舞、泰國樂舞、越南樂舞等演出形態。除此之外，國立臺灣博物館也與其它機構產生連結，並將藝術資源導入館中，例如：將文化資產局邀到臺灣於臺中文化資產園區演出的越南胡志明市音樂學院團隊，請到臺北於國立臺灣博物館大廳進行才子樂與占族樂舞展演；提供國立臺灣博物館場地予執行「韶聲越南才子樂團尋根計畫」的國立臺北藝術大學團隊進行成果展演；爭取駐臺北印尼經濟貿易代表處所藏之峇里巴隆獅（
      <em>barong</em>
      ）入館典藏，而巴隆獅又為峇里樂舞中極為重要的組成，故而國立臺灣博物館此典藏協議具有其代表性。綜覽前例，國立臺灣博物館雖是以典藏與研究作為主要導向的博物館，然而因其特殊的歷史脈絡，似乎也成為隱含世界音樂生命力的場域。
    </p>
      <h4>（六）國立故宮博物院南部院區</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      有別於國立故宮博物院北部院區的華夏廟堂傳統定位，南部院區則是在更為宏觀的亞洲藝術文化脈絡中取得置點，也在這樣的狀態下，南部院區積極與近鄰的東南亞藝術文化發生連結，當中不乏表演藝術，其中當然也包含了世界音樂。南部院區內有兒童創意中心，中心下常設印尼織品、亞洲陶瓷、亞洲茶文化、亞洲劇場等項目，其中亞洲劇場以印度史詩《羅摩衍那》（<em>Ramayana</em>）為主軸，延伸至南亞、東南亞表演藝術如印度偶戲、印尼皮影戲（<em>wayang
      kulit</em>）、泰國倥劇（<em>khon</em>）等，並展有陸上東南亞傳統樂器組。今年度南部院區以泰國作為策展重點之一，規劃「泰國月」系列活動，除以展覽方式呈現泰國的歷史脈絡、文化風土、藝術樣態，亦邀請來自泰國的表演藝術團體現場演出泰國倥劇與傀儡戲（<em>hun
      krabok</em>），以及搭配交響樂團伴奏的泰拳武打。另外尚有以印度史詩與泰國神話為核心的泰文化主題課程，共分三類，以繪畫為神話作創意性接龍、敲擊泰國杯形鼓並吟唱史詩、認識泰國皮影戲（<em>nang
      talung</em>）並實際製作等，同時邀請兒童文學博士洪瓊君主持《拉瑪堅》（<em>Ramakian</em>）故事劇場，帶領孩童以身體實踐與展演的路徑進入史詩神話之中。
    </p>
      <h4>二、臺灣在地世界音樂團體發展</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在臺灣，本身即有許多熱愛世界音樂的觀眾，並且產生具有專業的創作、教學、演奏、跨界能力的個人或團體。事實上，這些個人或團體並非在2019年才橫空出世或突然活躍，然而他們卻往往被視為「小眾」，而不見或少見於官方年鑑之正式記述。臺灣世界音樂樂團與專業演奏者的產生，或出於音樂愛好者的自我鍛鍊，如中東鼓樂演奏家馬儁人、西非曼丁鼓樂演奏家李建亨、西非旋轉沙鈴演奏家陳福壽等，或出於接受音樂學院體制內世界音樂、田野調查教育薰陶者如巫培誠（爪哇甘美朗<em>gamelan</em>）、黃筌琳（印尼安格隆搖竹）、蔡秉君（婆羅洲沙貝琴<em>sapeh</em>）、徐宿玶（日本箏<em>koto</em>）、劉穎蓉（日本尺八<em>shakuhachi</em>）等。而在臺灣大專院校之中，將世界音樂演奏與研究作為發展重心者，則是國立臺南藝術大學民族音樂學研究所與亞太音樂研究中心，故而下述臺灣世界音樂樂團名錄，或與該所相關、或創辦者本身即為該所出身的音樂家。</p>
      <h4>（一）<em>Asalato Taiwan</em></h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;西非旋轉沙鈴（
      <em>asalato</em>
      ）是源自西非的迷你掌中樂器，一條短繩兩端掛著達楂果的果實，裡頭放相思豆，藉著搖動而發出沙沙聲，同時配上果實之間的敲擊叩叩聲，敲打的節奏感即展現生命的活力。在西非，旋轉沙鈴僅作為歌唱或樂舞的伴奏，節奏相對簡單，傳到日本後，日本的西非旋轉沙鈴音樂玩家們在原有節奏基礎上，發展出更多複雜且更具技術性的演奏手法。西非旋轉沙鈴再從日本傳到臺灣，因此有<em>Asalato
      Taiwan</em>樂團的誕生。<em>Asalato
      Taiwan</em>，由臺南人陳福壽老師創立及開設工作室，且積極進行推廣教育、網路宣傳、團員徵召、產品開發、節目編排等工作，並營造互相砥勵進步的學習與「玩音樂」環境，故使團隊成員林汗、陳永、許皓程、鄭冠秀等深獲啟發。2017年首次赴日參與Asalato
      Night交流演出，後於2019年，三位來自<em>Asalato
      Taiwan</em>的西非旋轉沙鈴音樂玩家－陳永、許皓程、鄭冠秀，再度赴日參與舉辦於京都UrBANGUILD的2019 Asalato
      Night盛會，在大賽中以即興編排的精湛展現獲得大賽的個人組冠軍，2006年舉辦Asalato
      Night以來，首次由非日本玩家拔得頭籌。<em>Asalato
      Taiwan</em>除於學校體系內開設課程外，其演奏足跡也見於屏東新住民多元文化市集、2019臺灣燈會，以及臺灣各地世界音樂展演活動等。西非旋轉沙鈴於臺灣的推廣與展演，在陳君及其團隊成員的努力下已逐漸開花結果，同時也因為西非旋轉沙鈴樂器本身的輕巧易攜，以及演奏時的自由無拘與技巧性展現，而成為臺灣世界音樂中極具發展潛力的展演藝術。
    </p>
      <h4>（二）<em>Matahari </em>日央人兒世界樂團</h4>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<em>Matahari</em>
      一字為印尼文中的「太陽」，而該團的核心價值便是「願音樂如同陽光一般，跨越地理與族群的邊界，普照著每個愛好音樂文化的人。」該團由國立臺南藝術大學民族音樂學研究所出身的黃筌琳、蔡秉君創辦，先後參與的演奏者有王彥庭、陳聖元、林汗、陳承煦等。黃君在筆者的指導下，以及其2016年於日惹國立印尼藝術大學表演藝術學院（Fakultas
      Seni Pertunjukan, Institut Seni Indonesia
      Yogyakarta）求學時所受之啟發，2017年返臺後即邀集若干位同為世界音樂研究者、演奏者的同學加入其創辦之
      <em>Matahari</em>
      日央人兒世界樂團，並由筆者擔任樂團顧問，指導樂團發展方向並給予創作、展演的建議。該樂團使用之樂器多元，有爪哇甘美朗、印尼安格隆搖竹、日本尺八、西非旋轉沙鈴、西非曼丁金盃鼓（<em>djembe</em>）、巫毒鼓（<em>udupot</em>）、吉他、小提琴、婆羅洲沙貝琴，跨越的文化形態與音律體系甚眾，演奏足跡遍及臺灣東南亞文化藝術論壇、2019文化部東南亞諮詢委員會會議、高雄市立美術館「太陽雨」東南亞當代藝術特展，以及臺灣各地新住民與世界音樂展演活動等。2019年，
      <em>Matahari</em>
      日央人兒世界樂團與國立臺南藝術大學民族音樂學研究所合作，聯名申請文化部臺灣青年文化園丁計畫，並獲補助重返日惹國立印尼藝術大學表演藝術學院，與其下系所（甘美朗藝術學系、偶戲藝術學系）師生交流及展演。除展演外，
      <em>Matahari</em>
      日央人兒世界樂團亦致力於推廣教育，今年與財團法人屏東縣好好婦女權益發展協會合作，開辦印尼安格隆搖竹音樂培訓課程，並協助籌組印尼安格隆搖竹親子樂團。
    </p>
      <h4>（三）華之和音日本邦樂團</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;華之和音由徐宿玶老師與劉穎蓉老師組成，以日本邦樂的演奏、教學與推廣等工作為矢志。徐君與劉君均具中國音樂專業演奏背景，且皆畢業於國立臺南藝術大學民族音樂學研究所，就讀該所期間，由於民族音樂學對於多重音樂展演能力的要求，以及該所聘請的日本客座教授，因而接觸日本音樂，主修箏的徐君與主修笛簫的劉君分別學習日本箏、日本尺八，並且多次赴日向宮田耕八朗、渡邊治子等日本邦樂大師請益學習，成為臺灣少數的專業日本邦樂演奏家。華之和音自2012年正式立案以來，除專場音樂會外，更已多次於臺灣的日本文化藝術場展演，並籌辦臺日音樂交流活動，邀請日本傳統樂器如日本箏、日本尺八、三味線等之專業音樂家赴臺演出，亦帶領學生赴日親炙日本邦樂大師。徐君與劉君現於國立臺南藝術大學民族音樂學研究所與中國音樂學系開設日本箏與日本尺八演奏課程，培育臺灣的日本邦樂專業演奏者新血，讓學生藉由樂器學習與音樂演奏更加了解日本文化與聲響美學。2019年是華之和音成果豐碩的一年，參與或策劃許多教學、展演、講座活動，2019國立臺南藝術大學世界音樂研習營、於日本東京舉行的「華之和音海外師生音樂會」、文化部文化資產局無形文化資產講堂之日本音樂講座等。從上述眾例可見，在華之和音的努力下，作為臺灣視角下世界音樂的日本邦樂，已在臺灣有勃勃生機。</p>
      <h4>（四）非洲太暘鼓舞劇團與空空把（<em>Konkonba</em>）西非擊鼓樂團</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      同為推廣西非曼丁鼓樂的非洲太暘鼓舞劇團與空空把西非擊鼓樂團，各以臺南、臺北作為發展點，成為相互映照的雙葩。強大於十三世紀的曼丁帝國，地理位置涵蓋了今日的馬利、幾內亞、布吉納法索、象牙海岸、甘比亞、塞內加爾等國，是孕育西非曼丁鼓舞文化的沃土，而如此鼓舞文化並不僅作為表演藝術看待，更是當地群眾生活與交誼的日常，因此是臺灣西非曼丁鼓樂學習者必去之地，非洲太暘鼓舞劇團與<em>Konkonba</em>空空把西非擊鼓樂團曾於該地交流學習，而西非曼丁鼓樂大師Mamady
      Keita亦對上述兩團啟發甚深。非洲太暘鼓舞劇團由在臺南的西非曼丁鼓樂愛好者發起組成，並且熱衷於推廣與展演，團長李建亨的教學足跡遍及國立臺南藝術大學、台南應用科技大學、崑山科技大學、臺南市社區大學、高雄市社區大學、臺南市立延平國民中學等。2019年於臺南仁德籌劃TDF臺灣非洲鼓舞聚，匯集臺灣的西非曼丁鼓樂愛好者，再現西非當地以擊鼓「玩音樂」與溝通交流的場景。而位於臺北的空空把西非擊鼓樂團是一家族樂團，團名來自樂曲
      <em>Kokonba</em>
      ，意指極富智慧的大自然，期待透過西非曼丁鼓樂的自然悸動，散播音樂的正能量。空空把西非擊鼓樂團積極辦理或參與演出、工作坊與教學，其2019年活動甚多，如十週年紀念展演暨工作坊、空空把俱樂部入門課程、與非洲太暘鼓舞劇團共同策劃ILO臺灣非洲鼓舞聚前夜祭、空空把西非擊鼓樂團
      Ft. Sandy Mango &amp; The Cosmic
      Boat跨界專場音樂會，以及應邀於田中馬拉松、新店溪遊記、2019淡水環境藝術節、2019綠色生活悠遊節等活動場合演出，今年亦赴日本鹿兒島交流。除非洲太暘鼓舞劇團、空空把西非擊鼓樂團，尚有聲唄（<em>Senbe</em>）鼓舞樂團與<em>WassaFolli</em>臺中非洲鼓舞樂團，皆是西非曼丁鼓樂在臺灣的推廣重要團體，也足見在臺灣視角下的世界音樂範疇裡，西非曼丁鼓樂占有重要的一席之地。
    </p>
      <h4>三、教育的深化與民間的交流活動</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;前文談及官方政策構思與執行，以及若干世界音樂樂團的努力與成果，但若未有教育機器的系統性深化與更多的民間自發交流活動等層面之介入，世界音樂在臺灣的扎根與發展終究僅是失語的「小眾」。在教育機器系統性策略與執行的層面，筆者後續將以國立臺灣大學、國立臺北藝術大學、國立臺南藝術大學、台南應用科技大學、國立暨南國際大學、臺南市立崇明國民中學等校正式課程規劃對於世界音樂教育、運用、展演、推廣等面向，分點詮釋教育機器如何系統性地運用世界音樂作為認識、理解他者文化的載體，同時指出現階段的不足，並且剖析未來發展方向的可能性。</p>
      <h4>（一）教育體系</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在教育體系中，特別是大學的音樂系所或藝文中心，一直都是世界音樂教育與推廣的主體單位，以下將針對今年教育單位的系統性操作作一說明：</p>
      <strong>1.國立臺灣大學</strong>
      <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國立臺灣大學音樂學研究所在王櫻芬教授的主導下，成立兩個東南亞樂團，分別是臺大緬甸塞恩樂團（NTU
      <em>Hsaing Waing</em>
      Ensemble）與臺大永恆之歌甘美朗樂團（NTU
      <em>Gamelan Gita Lestari</em>
      ），今年分別於5月底與6月初進行成果展演。臺大緬甸塞恩樂團由中央研究院民族學研究所副研究員呂心純主持，並邀請緬甸音樂大師Aye Kyu、Thiri
