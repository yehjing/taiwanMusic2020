    <br>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化的推廣須在地深耕，TW-EGY中東傳統民俗舞蹈團由來自埃及的臺灣新住民何穆（Mohamed
      Mmadoh）擔任團長，與太太趙芯瑩共同經營舞團，帶領12名社區媽媽推廣正統的中東舞蹈，透過社區進行多元文化推廣與交流，是屏東第一個成立的新住民舞蹈團，為屏東好好婦女權益發展協會的重點扶植團隊。團長何穆今年帶領團隊赴印度參加第3屆CU韻律國際民俗藝術節（CU
      Rhythm International Folklore
      Festival）民俗舞蹈比賽，此次比賽共有25國隊伍參賽，臺灣首次受邀，該團隊以中東與臺灣融合的民俗舞蹈榮獲季軍。何穆自身的舞蹈專業，讓他在中東的文化與舞蹈推廣上更能得心應手，在社區文化推廣扎根，站上國際賽事舞臺、提升專業形象。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;隨著新住民發展基金所補助的「新住民及其子女海外培力計畫」之執行，至今已有9百多人受惠，今年有13個國家的新住民及其子女回到母國作文化交流的計畫執行，探索不同的風土民情與文化。新二代的興起讓在臺灣的新住民藝術表演邁入一個新的紀元，在過去純粹娛樂性質的演出中，如何提升專業度將會是未來最大的挑戰，文化傳承必須從文化的重視與認同開始，除非未來社會能協助新二代逐漸認同自己雙重文化的身分，才可能提升整體的新住民表演藝術。除新住民發展基金的運作，也有新住民開始將子女送回母國學習傳統音樂理論與樂器演奏，期盼新二代逐漸以他們各自所扎根的母國文化經驗作為傳承。如新二代林炘蓉、林家妤姊妹，他們的母親為泰國人，在2018及2019年的暑假由父母安排回泰國，分別學習泰式揚琴（
      <em>khim</em>）及泰式木琴（
      <em>ra nad</em>
      ），並在泰國親戚家居住，讓自己親浸於樂器學習的同時，也能從生活體驗中理解泰國社群文化。回臺灣後，姊妹一同在新住民市集上推廣泰國傳統音樂文化，表演之餘也在父母的泰國美食攤位協助販售泰國傳統美食，在泰國學習的期間，除音樂培訓也學習當地菜餚的烹飪技術，讓活動中的參與者在聲音以外，更能從鼻息與味蕾完整體驗到泰國的文化之美。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;師資上的精進也提升藝術表演的專業度，2019年底由屏東縣好好婦女協會組織西爪哇傳統樂器印尼安格隆搖竹的親子團，以不限國籍、年齡與音樂能力背景的參與來提升吸引度，同時由黃筌琳及蔡秉君擔任教師。這二位皆經常往返印尼與當地音樂家學習、交流，並將所學帶回臺灣，屏東縣好好婦女協會借重如此專業師資，期盼印尼安格隆搖竹親子樂團的器樂學習更能系統化，並提升專業性；而樂團亦可走入社區，除參與不同公益性質的演出，也作為連結不同社群的載體，合力參與文化推廣，增進人與人的緊密關係，達到一舉數得的新住民文化展演效益。</p>
      <h3>結語</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;整體而言，雖然世界音樂、跨界音樂與新住民音樂活動已受到臺灣社會的關注，但不可否認的事實是，臺灣雖然已經是一個多元的社會體系，但臺灣在政府政策、社會制度、文化平臺、人文思維上卻尚未達到多元社會的要求，而這樣的一個結果當然也就會影響到這些新形態音樂的發展。近幾年來，由於臺灣政府強調「新南向政策」也因此投入大量的財力與人力，因此無論是在世界音樂、跨界音樂以及新住民音樂等領域皆可以看到「新南向」的影子，然而由於政府對於東南亞文化藝術概念與教育不足，導致各級學校音樂教育中對於世界音樂、跨界音樂與新住民音樂教育的忽視，加上文化部、教育部、內政部、勞動部以及地方政府雖然有心強調「新南向」、「多元文化」概念，但卻也由於對文化藝術觀念的不足，往往把音樂、舞蹈、戲劇等表演藝術形態，停留在例行性公務活動、外在形態的呈現、對新住民與東南亞移工的照顧等福利政策式的概念。然而，世界音樂、跨界音樂、新住民音樂等表演藝術形態卻與社會結構、身分認同、宗教信仰、區域語言、歷史環境、生活習俗、各類藝術形態文化內涵有著密切關係，然而過去臺灣卻是以政治、經濟、工業、旅遊與教育招生等較為現實層面的思考為主，也難怪當各級政府單位努力在從事「新南向政策」相關藝術文化表演時，卻往往無法達到應有的效果，甚至可能會發對「異文化」的不尊重的現象。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不可否認地，臺灣各級政府、大學教育體系、民間藝文團體，都十分地努力於世界音樂、跨界音樂與新住民音樂文化的發展，雖然尚有許多努力的空間，但也確實可以看到一些成果的展現。若能在政策制定以及表演藝術的外在呈現外，能夠提供更多的演出平臺、表演藝術文化的內涵、身分認同以及主事者對文化尊重，相信臺灣的世界音樂、跨界音樂與新住民音樂能夠有更加寬廣的發展空間。</p>
