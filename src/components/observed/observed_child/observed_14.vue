      <h3>貳、多元藝術元素結合的跨界音樂</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;跨界音樂作為臺灣音樂年鑑的子項目之一，於去年度（2018）已是如此，然而何謂「跨界」？其與「跨文化」、「跨媒材」、「跨領域」等皆帶有「跨」字等學術名詞又有何異同？當某種世界音樂形態與另一種較為「大眾」的音樂形態發生「跨界」時，其定義與書寫的歸屬應當如何判定？以上問題一直都是學界所關心的議題。本處欲透過論跨界形式與編制的多元性、表演藝術的「組合、磨合、聯合、融合」四部曲、「『界』為何物？」：再探「跨界」與「跨文化」的可能性等三個面向，結合音樂學者陳慧珊所歸納之六種「跨」的樣貌或可能性加以論述，而組合、磨合、聯合、融合之四部曲，乃是筆者今年蹲點觀察的現象與心得，並認為跨界展演不能流於形式，而是更該有深層的文化視野作為基礎。</p>
      <h4>一、論跨界形式與編制的多元性</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      「跨界」本身即是作為由多重元素組成的複合體存在，在筆者今年度的田野蹲點觀察與網路資料閱讀的過程中，將所見形式與現象歸納為三大面向，分別是「流行」音樂與「傳統」音樂、「跨媒材」之間的展演形式、「跨文化」之間的音樂呈現，並試各舉一至二例說明且加以分析。此處的目的仍停留在表演藝術表面的「跨界」形式，並著重參考美國文化人類學、象徵人類學大家克里弗德．紀爾茲（Clifford
      Geertz, 1926-2006）所提出的「深描（thick description）」筆法（Geertz,
      1973），以稠密的書寫方式描繪跨界的形式，為後續建構跨界「意義網絡」及其文化脈絡分析進行預備工作。
    </p>
      <h4>（一）「流行」音樂與「傳統」音樂</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「流行」與「傳統」此二相對存在的概念，未必跨越文化或領土的邊界，也可能是同一空間裡不同時間下所生成的產物而同存於當下。以具有文化部「重要傳統藝術布袋戲類保存者」、「古典布袋戲偶衣飾盔帽道具製作技術保存者」兩項國家榮譽的臺灣傳統布袋戲大師陳錫煌為例，其掌中班今年4月底與臺語搖滾樂團拍謝少年於臺灣文博會合作演出，是為「陳錫煌尬拍謝少年」，在音樂上，可知是當代流行與北管鼓吹的競奏與共融，而在表演藝術的整體面貌來看，則是布袋戲的細膩敘事與搖滾樂的奔放歌詞並存。而兩者在合作過程中並不單純將聲音作為交流素材，而是嘗試立於對方置位與文化脈絡，來思考音聲的創造路徑。</p>
      <h4>（二）「跨媒材」之間的展演形式</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;不同的展現手法或不同的藝術載體，皆可以看作是「跨媒材」之間的展演形式，諸如科技與藝術的碰撞，或是表演藝術（如音樂）與視覺藝術（如繪畫）之間的交匯，咸能視之。以今年榮獲傳藝金曲獎（第三十屆）「最佳跨界音樂專輯」與「最佳編曲」雙料獎項的《海翁繪》為例，即是音樂與繪畫之間「跨媒材」的對話，借音樂學者謝斐紋（2007）語，音樂與繪畫的若干層面－如線條與旋律、色彩與織度、形式與曲式等－交織出互文的藝術景致。小提琴家張雅晴受到作為知名畫家的父親張金發之啟迪，以其一系列臺灣－生於太平洋的「海翁（鯨魚）」－人文風土畫作發想，同時身體力行，走過張金發當年繪畫時上山下海的路徑，以採集臺灣本土歌謠過程並感受父親的畫境。透過西方作曲技法與小提琴、鋼琴展演的模式，將畫作中的情境以樂聲作為呈現載體外發。</p>
      <h4>（三）「跨文化」之間的音樂呈現</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      跨越時空發展脈絡與文化語境產物的結合，便是「跨文化」的音樂呈現，臺北兩大具領頭羊地位的國樂團－臺北市立國樂團與臺灣國樂團今年皆作此嘗試，而兩團先前已曾分別與泰國（2017）、越南（2017）的傳統樂團進行過跨文化音樂交流。臺北市立國樂團今年由鄭德淵教授策劃「萬島綺想：不只甘美朗」印尼專場音樂會，其中最大的嘗試莫過於甘美朗大協奏曲《銅聲絢彩》，將兩種織體結構、音聲哲學、生成脈絡完全不同樂團形式－爪哇甘美朗樂團、大型現代國樂團－並置。而在施德華教授的促成下，臺灣國樂團與日本北之台雅樂会的合作也是如此。兩團分別與東南亞（印尼）、東北亞（日本）的大型傳統樂隊共演，皆要面對若干相同的普遍性問題：作曲家如何妥善運用這兩種音聲元素？而指揮家如何在排練現場實際調度聽覺呈現？是否能同時在兩種「地方知識」（Geertz,
      1983）之間來去自如地轉換思維，這已不僅是「跨界」的層面爾爾，而是「跨文化」－是否能夠站在對方的文化脈絡中思考？
    </p>
      <h4>二、表演藝術的「組合、磨合、聯合、融合」四部曲</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      「跨界」本身既然是作為由多重元素組成的複合體存在，那麼考慮到多重元素之間的結構模式－組合、磨合、聯合、融合－便是不可避免的。筆者在田野蹲點觀察與網路資料閱讀過程中，概括出跨界的「組合、磨合、聯合、融合」四部曲，乃是受到法國後現代主義哲學家吉爾．德勒茲（Gills
      Deleuze, 1925-1995）《千高原（
      <em>A Thousand Plateaus</em>
      ）》「流變（becoming）之線」（Deleuze &amp; Guattari,
      1980）的啟發，如此流變之線存於拓樸學域外，是由特異點所組成抽象線（楊凱麟，2006），是故此線並非由兩點以知識論上直觀地構成，或是作為連接兩點的橋梁。拉回到討論「跨界」的當下，多重文化或展演元素、手法共同流變而成的複合體，既有多重元素的成分，又能形成一種自身獨有的藝術美學而不至於收編進單獨一項元素之下，即是「跨界」的最終觀照。
    </p>
      <strong>（一）組合</strong>
      <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;當兩種（或以上）文化元素或展演手法接觸時，其表面狀態僅是一種藝術的組合，是「跨界」的第一步，一如前文所提及的傳統布袋戲與臺語搖滾樂、音樂與繪畫、國樂團與爪哇甘美朗樂團或日本雅樂團等等，若僅有此第一步卻不經適當而深入的調配，則可能會淪於僅有「跨界」之名，變成為「跨界」而「跨界」的空殼，便失去其「跨界」的意義，所幸，前揭諸例皆並不是只有「組合」的階段，而是縱深更為廣袤的發展。然而，也不能全盤否定「組合」的存在，因為「組合」提供碰撞的可能性，進一步提供發生磨合、聯合、融合的可能性。</p>
      <strong>（二）磨合</strong>
      <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;生成於兩種（或以上）文化脈絡的藝術產物組合接觸之後，因其外在展現形式與內在美學思維的差異而產生扞格，因此進入「磨合」階段。而具游離性質的「磨合」階段又最為關鍵：若磨合失敗，則退回流於形式的「組合」階段；反之，若「磨合」成功，方有可能邁向跨界的下一步。以臺北市立國樂團與印尼爪哇甘美朗樂團的演出為成功案例，此二種樂團種類慣習的節奏形態、音階音準、樂曲結構乃至於整體音聲哲學方面，皆不盡相同，因此，排練當下即是最直接的「磨合」當下，指揮家作為跨音聲調配者，使兩種迥異之音聲逐漸達到合宜的狀態。</p>
      <strong>（三）聯合</strong>
      <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從有如通過儀式一般的「磨合」狀態穿越後，始進入已有一定程度相互交織的「聯合」階段，而這也是大多數跨界展演所呈現的樣貌。仍是以臺北市立國樂團與印尼爪哇甘美朗樂團的演出作為成功「磨合」進而導入能夠真正「聯合」展演的案例分析。排練過程中，透過指揮家的調配與兩團演奏家的互相聆聽，逐漸排除所謂節奏上、音準上的，儘管如此「扞格」未必是負面的「問題」，然而將其排除後，在不抹滅二者內在音聲哲學的前提下，而達到「合宜」的狀態，其中牽涉到是否以他者「地方知識」角度思考，以及文化相對論的反思。</p>
      <strong>（四）融合</strong>
      <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      最終是「融合」狀態，也最接近德勒茲所提出的「流變之線」。在前揭眾例中，傳統布袋戲與臺語搖滾樂之間、音樂與繪畫之間、國樂團與爪哇甘美朗樂團之間或與日本雅樂團之間，每種藝術形態是否自成一個特異點？而這些特異點能否能產出一條具有能動性並且不斷在閾限之間轉換的「流變之線」？筆者認為，在這一年的田野蹲點觀察與網路資料閱讀經驗裡，尚不能很武斷地直接定言，因為如此「跨界」的「流變之線」生成若以時間的瞬間切面來看，它是存在的，一如國樂團與甘美朗教之而成的聲響（圖2），然而拉長時序，則須持
      <br clear="ALL" />續觀察與不斷商榷。
    </p>
