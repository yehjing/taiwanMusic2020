    <h4>（三）國立傳統藝術中心</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國立傳統藝術中心與文化資產局齊頭並進、各有所司，而前者又較後者更著重於展演本體。同時，由於該機關的定位與性質，國立傳統藝術中心長期投入國內外傳統藝術－包含世界音樂－的策展與推廣，雖然其活動未必直接與新南向方針有絕對關連，但歷時已久的亞太樂器常設展、亞太傳統藝術節皆與東南亞表演藝術有著密切的關聯性。亞太樂器常設展長期展出臺灣（原住民族、漢族）、東北亞（日本、韓國）與東南亞（泰國、緬甸、柬埔寨、寮國、越南、菲律賓）等地域之傳統樂器，以臺灣為東亞島鍊上的地理軸心，藉由樂器向南北開展延伸並共構出一幅亞太地區豐富多元的音樂景觀，展區上分「島嶼樂記憶」、「亞太樂風情」、「互動樂體驗」三塊，在視覺、聽覺以外，透過互動科技的介入，使觀眾有機會體驗亞太音樂之美。當然，展中所謂「亞太音樂」，扣除掉臺灣原住民族與漢族成分，東北亞與東南亞皆屬臺灣本體視角下的世界音樂範疇。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;而亞太傳統藝術節，則直接透過演出來呈現亞太傳統表演藝術之多種面貌，今年度的演出團體國家有喬治亞（東歐）、印度（南亞）、越南（東南亞）、韓國（東北亞），分別演出複音音樂、卡貝利亞民間歌舞、北寧官賀民歌、羌羌水月來歌舞等，並與臺灣本土的布農族音樂、屏東滿州民謠進行對話。更重要的是，在展演以外，國立傳統藝術中心亦針對臺灣觀眾舉辦複音音樂、卡貝利亞民間歌舞、北寧官賀民歌、羌羌水月來歌舞的文化體驗工作坊，結合聆聽與觀賞的基礎，進一步透過身體的實踐，能夠加深對於上述世界音樂項目的認識與理解。另外，在今年由國立傳統藝術中心主辦的臺灣戲曲藝術節中，隸屬於該中心的臺灣國樂團亦首度與來自日本的北之台雅樂會合作「雅正之樂－日本雅樂的音樂風情」音樂會兩場，分別是雅樂專場與兩團的跨文化、跨音聲聯演。由上述例子可見，國立傳統藝術中心在推動國際間傳統藝術交流的過程中，也將臺灣視角下的各種世界音樂展演形態，以官方的位置系統性地帶入臺灣。</p>
      <h4>（四）國立臺南生活美學館（文化部所屬國立臺灣美術館）</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
      隸屬於國立臺灣美術館的國立臺南生活美學館，是為四級機構，統管南臺灣7縣市（嘉義縣、嘉義市、臺南市、高雄市、屏東縣、金門縣、澎湖縣）之生活美學、文化藝術展演、文化創意產業、社區營造等業務。近年來該館關注轄區內的新住民母國文化，同時籌劃新住民文化培力暨國際藝術巡迴展演計畫，邀請來自泰國、越南、印尼等東南亞國家的流行音樂或傳統藝術之表演團體赴臺灣演出交流，同時亦開設東南亞樂器培訓班，如107年度的印尼安格隆搖竹（<em>angklung</em>）、108年度的泰國杯形鼓（<em>thon
      &amp;
      rammana</em>）等，文化講解與樂器學習皆作為該課程重點，也提供觀眾有直接學習世界音樂文化與樂器的途徑。今年度由國立臺南生活美學館主導並巡展於該館、國立彰化生活美學館、桃園市新住民文化會館的「日用之美－東南亞生活美學展」，定義「日用之美」的其中一個面向為「音律美」（國立臺南生活美學館，2016），因此該展區之展品除有前述的印尼安格隆搖竹、泰國杯形鼓之外，更有越南民俗音樂中的重要樂器石板琴。故而，國立臺南生活美學館雖隸屬於視覺藝術重鎮、建構臺灣美術史的重要機構國立臺灣美術館之下，然在推廣生活美學及與其不能切割表演藝術、音律藝術同時，也作為世界音樂的推廣、教育、交流、展演的一方寶地，而這些世界音樂亦回過來成為生活美學的養分。
