    <h4>三、「『界』為何物？」：再探「跨界」與「跨文化」的可能性</h4>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;音樂學者陳慧珊（2016）曾以六個英文字（根）為基礎，提出六種「跨界」的定義或面向，分別是含混無章的mix、主客體置位易辨的cross、多重主體性並置的inter、自體變形並與他者交融的multi、文化思想等內在交集的hybrid、外在形式與內在思想皆蛻變為嶄新樣貌的trans。上述六種定義或面向是陳慧珊以長期的觀察與研究歸納而出，實可作為判斷「跨界」深淺程度與執行方向的基準，亦能見內在的文化思維作為背後推動「跨界」程度的主導因素之一，故而筆者以此發端，從「跨界」與「跨文化」作為華而不實的名詞、對於他者文化的深度認識與應用、期待怎樣的「跨界」與「跨文化」等三個角度進入命題。</p>
      <strong>（一）「跨界」與「跨文化」作為華而不實的名詞</strong>
      <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;並不涉及內在文化底蘊的思考，而僅關注於外在形式的拼湊，不免落入削足適履之境，同時也使兩個具有能動性的特異點無法產生關係。筆者今年蹲點所觀察到的其中一檔節目即是如此，以存於東南亞戲劇與華人戲曲中共有的動物形象作為出發點，此構思本身具有創意，然而在展演的實踐過程中，該共有動物各自在兩種戲劇形式中展現武打身段，最後再對打，純粹是形式上的拼貼，一如前文提及之mix，無法見其在兩種戲劇形式時而應產生的更高層次之文化思考與對話。</p>
      <strong>（二）對於他者文化的深度認識與應用</strong>
      <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「跨界」的呈現不應停在形式上的交流，而是該對於他者文化有深度認識，才能加以應用。一如拍謝少年與陳錫煌傳統掌中劇團的合作，以其音樂本體為例，搖滾樂與北管樂相遇時，雙方互相交流/教導對方自己所慣習的音樂語彙與手法，進而產生交織。又如臺北市立國樂團與三位印尼音樂家、國立臺南藝術大學爪哇甘美朗樂團共同演出的甘美朗大協奏曲《銅聲絢彩》，該曲由馬來西亞華樂作曲家江賜良創作，因其專業背景，故對國樂創作的方向知之甚詳，在此基礎上，江君亦能針對爪哇甘美朗的文化脈絡、織體結構、聲響哲學等方面深入探究，進而有能力在國樂與甘美朗音聲之間求得一條跨界的流變之線。</p>
      <strong>（三）期待怎樣的「跨界」與「跨文化」</strong>
      <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「跨界」未必等同於「跨文化」，後者具有很直接的文化指涉，然而前者若不思考文化或其內在語境的問題，便會如同前揭談及之東南亞戲劇與華人戲曲中共有動物形象之展演，而流於僅存外在形式之空殼。故而，筆者所期待的「跨界」與「跨文化」展演，能夠呈現所用媒材的內在意義，或是所用兩種（或以上）藝術元素之間的深層對話與共振。除創作者、展演者本身的創意發想之外，是否能跳脫視角，藉由他者的「地方知識」觀點以看待文化現象與本質，進而體現於「跨界」的構思與實踐上，以文化底蘊作為基礎，才是令人期待的。</p>
    <h3>參、強調身分認同的臺灣新住民音樂文化</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;移民社會的臺灣，新住民為其注入新的文化元素，讓藝術文化多元並茂。新住民是以姻親關係及移民定居臺灣的外籍人士，根據內政部移民署的統計調查，至2018（民107）年底前臺灣新住民的人數約有543,000多人，若不以同國籍而言，已是臺灣的第五大族群。近三十年來的國籍統計，最多人數來自中國佔63%，其次為越南19%、印尼6%、港澳3%、菲律賓2%、泰國2%、柬埔寨1%、日本1%、韓國0.3%與其他4%。<sup>1</sup>這樣的數據呈現出不同民族的文化多樣性，其中便可看出以中國及東南亞各國的人數居多，這也反映在眾多新住民相關活動藝文團體的國籍分布，不論是舞蹈團體或音樂團體，都能見到以中國及東南亞各國的文化團體為主。其中又以不同文化的節慶延伸出不同的活動，如泰國的潑水節、印尼國慶日、越南國慶日以及伊斯蘭教開齋節等活動，這些表演藝術活動已成為臺灣音樂文化景觀的一部分。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除新住民外，在臺工作的外籍人士也是東南亞文化於此發展的助力，根據勞動部勞動力發展署的統計資料，至2019（民108）年度10月底的外籍勞工人數為716,000多人，其中人數最多為印尼，接次為越南、菲律賓及泰國。<sup>2</sup>這些大量的移工人口乃為一個不可忽視的群體，他們將家鄉的飲食文化帶入臺灣，慢慢影響臺灣人的飲食習慣，各種東南亞小吃店及民生用品商店林立，形成小型東南亞文化商圈，也帶動在地東南亞文化發展。雖然移工可運用的休閒時間不多，但仍然能在一些東南亞節慶活動表演上看到少數的移工表演團體，而臺灣獨立音樂工作者黃偉傑入圍去年（2018）金曲獎最佳年度專輯獎項的《夜色》，其中也有印尼移工參與音樂製作，甚至在若干臺灣傳統宮廟慶典表演也可以看到移工的身影。移工音樂活動目前仍屬於圈內籌劃與辦理的範圍，公開活動並不活絡，政府公家機關辦理的東南亞活動仍是以新住民為主要參與者。
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年是臺灣多元文化綻放的一年，同時也是一個使許多社會群體產生不同未來走向的分水嶺。我們可以看見無論是新住民自組團體、推倡移工權益的地方組織，或在地經由社福力量協助跨國婚姻問題的非政府組織，都不斷在幫助提倡將不同國家與民族的文化融入臺灣，而各大專院校外籍留學生的組織力量也藉由從校園出發，讓更多學子瞭解臺灣身於花彩列島中心，是如何吸收和囊括各種不同文化的洗禮。通常最容易影響一般人目光，大部分的活動即是營造視覺與聽覺的綜合性演出，以及藝術工作坊，直接以身體觸覺導入他者經驗，更甚者會有推廣群體運用在地食材，經由味覺與嗅覺上的刺激，讓參與民眾從最直接的感官－五感並重地體會不同民族的文化饗宴。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;但如同前言所述，正由於臺灣島的地理位置與文化慣習，大眾對於資訊的吸收與接納度習慣快速也短暫的方式，以至於整個社會社群都在不斷以「生產資訊」的模式在增加文化符碼的曝光度，進而逐漸流於形式性與公式化，如果大眾個體不是對於某種文化領域有著大量興趣或關注，其實多半無法更深入地理解，只能從整個「文化外貌」去接近；而公家機關有時也為因應大部分民眾對某種文化外在的吸引去作推廣，而邀請了不符合現場時宜的團體，或是舉行「去文化脈絡」的演出形態，更容易造成臺灣一般民眾誤解，只能以「再製的印象包裝」去理解他國文化。接下來，筆者依序先將今年臺灣各處較為主要的新住民或移工活動說明介紹，再依序剖析觀察到其問題及細節。</p>
