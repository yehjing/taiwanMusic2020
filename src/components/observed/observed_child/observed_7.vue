    <h3>
      參、族群音樂發展的深入剖析
    </h3>
    <p>
      <strong>一、音樂推廣與交流的嶄新境界</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;年鑑的音樂紀錄，有很多活動的辦理地點是在偏遠地區或是深山裡的部落，像是排灣族「五年祭」（屏東縣來義鄉南和村白鷺部落）、排灣族「聽VUVU在唱歌－高士部落傳統古謠紀錄與保存計畫」（高士部落集會所）、大武壠族「小林夜祭」（高雄市甲仙區平埔文化園區小林公廨）、魯凱族「卑南鄉108年達魯瑪克部落豐年祭」（達魯瑪克文化廣場）等等，這些偏遠地區的活動為什麼卻為人所知，且能吸引許多外地人參加，主要原因是當前科技的進步、網路的發達以及社群軟體的普及所致。以觀眾來說，透過社群軟體或是網站，活動前可以下載節目單以及活動相關資訊，或是祭典相關規定，這樣的資訊取得，讓外地人的祭典之行可以更加便利。對於因為忙碌無法參與部落活動的人來說，有沒有什麼可以補教的方法，答案是肯定的，社群軟體的「直播」功能，可以同步的觀賞到活動的實況，如此一來，部落的活動可以零時差的傳到都市，都市的音樂會同樣也可以同步的傳到偏遠山區，所以對現代人來說，科技的進步，讓城市和鄉下的文化活動越來越緊密，一種「地球村式的文化觀摩形式」的理想隱然成形。</p>
    <p>
      <strong>二、蓬勃的</strong>
      <strong>平埔族群復振活動</strong>
      <br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;平埔族群方面，我們熟知且已復振文化多年的族群為馬卡道族、大武壠族、西拉雅族等，然而，今年的紀錄出現了許多我們極少聽過的族群辦理祭儀活動，像是巴宰族、拍瀑拉族、道卡斯族、噶哈巫族等，巴宰族拍瀑拉族祭儀活動文化政策各一則，道卡斯族展演活動兩則祭儀活動和文化政策一則，噶哈巫族展演活動祭儀活動及文化政策各一則。馬卡道族的紀錄，出現了車城鄉後灣地區和臺東縣池上鄉大坡地區的馬卡道族祭儀活動。儘管，整體而言平埔族的年鑑紀錄並不多，但是我們卻發現，大環境本土意識的高漲，以及平埔族群自身燃起的「尋根」思維，越來越多的平埔族群，會以辦活動的方式，或是以祭儀活動，凝聚族群的尋根意識，重建該族原來的樣貌，透過活動，也讓下一代的族人了解自己的身分，重新找回族群的榮耀。
    </p>
    <p>
      <strong>三、中小</strong>
      <strong>學生國外得獎的榮耀</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從幾年前開始，排灣族和魯凱族的學校合唱團體，開始嘗試到歐洲或東亞國家，參與各地辦理的歌唱比賽，這些比賽主要以合唱競賽為主。今年的紀錄當中，我們看到了幾個令人振奮的消息，這些紀錄都放在「國際交流」當中，包含魯凱族「屏東青葉國小合唱團於『2019維也納國際合唱比賽暨音樂節』獲得金獎第一名」（奧地利－國立維也納音樂暨表演藝術大學、維也納皇宮、布拉提斯拉瓦皇宮、布拉格皇宮）、排灣族「屏東希望兒童合唱團於『2019勃拉姆斯國際合唱音樂節暨合唱比賽』得獎」（德國）、排灣族「WOMADelaide音樂節－泰武古謠傳唱」（ADELAIDE, AUSTRALIA）、排灣族「2019 夏威夷Honolulu藝術季」（Hawaii Convention Center stage）。這麼多得獎的榮耀，應該喚起教育當局的重視，讓這些歌唱方面資賦優異的孩童，能有更多的機會往音樂之路邁進。&nbsp;&nbsp;&nbsp;</p>
    <p>
      <strong>四、原住民音樂與西方藝術音樂的交融</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年的音樂紀錄，有關原住民音樂與西方藝術音樂的交融，以及不同型態的創新表演紀錄，值得我們注意。以往，原住民音樂被歸類為傳統音樂，因此表演形式是以呈現古老部落精神為主，表演者也盡量模擬傳統生活樣貌，這樣的表演持續了很長一段時間，而這幾年，原住民音樂表演出現了許多與西方藝術音樂結合的範例，這些紀錄如下：第一，排灣族「來唱歌吧！泰武古謠傳唱－唱進WOMAD行前首發場」（衛武營國家藝術文化中心音樂廳），本演出下半場為「跨界動感版」，由四位知名樂手演繹全新編曲，分別為吉他、大提琴、擊樂與鍵盤。泰武古謠傳唱優美清亮的歌聲時，又加入了輕電音，整體的氛圍也顯得歡樂輕鬆。第二，排灣族「《海翁繪組曲》的原民意象與本土關懷座談會」（國立屏東大學音樂系圓廳），作曲者蔡淩蕙從所採集得來的原住民古謠中，歸納出與張雅晴的父親張金發先生畫作氛圍相襯的旋律，進而創作出幾首有如小詩般，各具風格與特色的音樂，畫作的標題以及古謠的原本詞意，也提供了創作意象的聯想，使《海翁繪》凝聚了豐富的色彩。第三，排灣族「口鼻笛大師傳承展演－在氣息與指尖的相遇」（臺灣原住民族文化園區娜麓灣歌舞劇場），這個演出，有別於以往口鼻笛多以純演奏方式之呈現，內容主要闡述「口鼻笛之傳說故事、文化意涵及面臨消逝到重新復振之過程」，本劇嘗試將口鼻笛吹奏融入當代生活與環境，透過音樂舞蹈創新融合，展現新舊兩個不同時代的表達。第四，排灣族「驚嘆樂舞－臺灣原住民樂舞饗宴」（國父紀念館），本次演出的所有團隊的傳統歌謠，包含春日太陽古謠隊、牡丹國中古謠傳唱隊、賓茂VASA東排灣樂舞團以及屏東縣草埔部落青年會之歌曲，均由灣聲樂團編曲與伴奏。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以上的例子，我們發現到原住民音樂以多樣的形式結合西方藝術音樂，傳統音樂呈現更多的可能性，發展空間無可限量。</p>
    
    <h3>
      <strong>結語</strong>
    </h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年的年鑑紀錄，排灣族與魯凱族展現了旺盛的生命力，文化活動的質與量，都有了豐碩的成果。兩個族群以外，拉阿魯哇族、卡那卡那富族，以及平埔族的馬卡道族、西拉雅族、大武壠族、巴宰族、噶哈巫族、拍瀑拉族等，這些族群人數少，族群規模小，但是卻也也呈現了平日少見的祭儀以及演出活動，對外宣示自身的文化主體性。</p>
    <p>&nbsp;&nbsp; &nbsp;透過音樂紀錄的分析與歸納，我們看到了音樂推廣與交流的嶄新境界，當前科技的進步、網路的發達以及社群軟體的普及，偏遠地區的活動也能傳達到各處。從觀眾的角度來說，由於社群軟體以及網站的幫助，活動前可以下載節目單以及活動相關資訊，非常便利，而對於因為忙碌無法參與部落活動的人來說，社群軟體的「直播」功能，可以同步的觀賞到活動的實況，於是，一種「地球村式的文化交流」的理想即將成形。年鑑紀錄上我們發現到了蓬勃的平埔族群復振活動，許多我們極少聽過的族群辦理祭儀活動，像是巴宰族、拍瀑拉族、道卡斯族、噶哈巫族等，儘管這些族群的紀錄數量相對於排灣族等大的族群並不多，但是我們卻發現，大環境本土意識的高漲，以及平埔族群自身燃起的「尋根」思維，越來越多的平埔族群，重建該族原來的樣貌，重新找回族群的榮耀。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;年鑑紀錄上我們也觀察到了中小學生國外的得獎榮耀，魯凱族屏東青葉國小合唱團於「2019維也納國際合唱比賽暨音樂節」獲得金獎第一名、排灣族「屏東希望兒童合唱團於「2019勃拉姆斯國際合唱音樂節暨合唱比賽」得獎、排灣族泰武國小合唱團參加「WOMADelaide音樂節」。這麼多得獎的榮耀，應該喚起教育當局的重視，讓這些歌唱方面資賦優異的孩童，能有更多的機會往音樂路邁進。年鑑紀錄還有值得探討的是原住民音樂與西方藝術音樂的交融，排灣族「來唱歌吧！泰武古謠傳唱－唱進WOMAD行前首發場」（衛武營國家藝術文化中心音樂廳），下半場為「跨界動感版」，由四位知名樂手演繹全新編曲。《海翁繪組曲》當中，作曲者蔡淩蕙結合張雅晴父親張金發先生的畫作，以及原住民吟唱素材，以西洋古典技法創作出一首首的篇章。排灣族「口鼻笛大師傳承展演－在氣息與指尖的相遇」當中，將口鼻笛吹奏融入當代生活與環境，透過音樂舞蹈創新融合，展現新舊兩個不同時代的表達。排灣族「驚嘆樂舞－臺灣原住民樂舞饗宴」，所有演出團隊均由灣聲樂團編曲與伴奏。從以上的現象可以發現到原住民音樂與西方音樂交融激發出燦爛的火花。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年的年鑑計畫，我們看到了南部原住民族&shy;－排灣族、魯凱族、拉阿魯哇族、卡那卡那富族，以及平埔族－馬卡道族、西拉雅族、大武壠族、巴宰族、葛哈巫族、拍瀑拉族等等，在展演活動、祭儀活動、推廣教育、傳習活動、國際與兩岸交流、研究與出版、文化政策以及多元活動方面，豐富而多樣的音樂文化表現，對於這些族群來說，2019年真是個「豐收」的一年。</p>
    
