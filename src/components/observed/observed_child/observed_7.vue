    <p class="bui">
      平埔族群是分布在蘭陽平原、東北角、北海岸、臺北盆地、西部海岸平原到臺南、高雄、屏東一帶的族群，南島語族（Austronesian Lingustic Family）的一支。由於平埔族群所住地區，地緣上均位處濱海的因素，平埔族群與亞洲其他地區的南島語族，一直以舟船互有來往；同樣也因地緣關係，當臺灣浮現於世界歷史的舞臺之後，受到大量來臺之外來人群政治、經濟、語言、文化等力量的衝擊。因此，自17世紀開始，平埔族就面臨了族群文化認同，族群文化抗爭、消失，以及族群文化保存的危機。
