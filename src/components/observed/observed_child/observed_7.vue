    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;卡那卡那富族與拉阿魯哇族一樣，是臺灣原住民人口數極少的族群，拉阿魯哇族和卡那卡那富族申請「正名」，政府於2014年（民103）6月26日正式宣布，認定各為獨立的族群。拉阿魯哇族為第十五族，卡那卡那富族為第十六族，目前登記為卡那卡那富族之人口數約有357人（2019年（民108）9月），每年的河祭和米貢祭已成為常態性的祭典。目前，卡那卡那富族（kanakanavu）的家屋tanasa約八成以上位於今日高雄市那瑪夏區境內的瑪雅與達卡努瓦兩里內的兩處大聚落，就是tanganua與vangacun，其餘一些家屋則散布在tetencu、lapinia和cipaku等地。而設籍在那瑪夏區的卡那卡那富（Kanakanavu）族人總數約四百餘人，若含設籍在外縣市的族人，估計應有五百餘人。（林曜同，2013，121-122）</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;河祭的神話故事，依據卡那卡那富族翁博學耆老所述：</p>
    
    <p class="bui">大約三百多年前，一位名字叫做namasia的年輕人到河邊，他發現到河裡有一隻大鱸鰻，鱸鰻形體非常大，當年輕人看到這麼大的動物，嚇得當場暈過去，過了一陣子他才醒起來，慢慢拖著身軀回家。因為在河邊驚嚇過度，回家後，他開始生病，過了兩三年去世。部落族人為了紀念這個年輕人的事件，所以這條溪就以他的名字稱作namasia，正是為了紀念這個年輕人的大發現。這個事件以後，族人們有了一些警惕，第一個就是透過這個故事的流傳，引發了族人對於河流更多的關注。第二是對於河神的尊重。每當雨季來臨，河流周邊的石頭峭壁，容易造成坍方，因此為了祈求河神保佑河流在任何時期完整不崩壞，祈求河神保佑族人下河平安並能滿載而歸，因此有了這樣的河祭。</p>
    
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外一個每年例行性辦理的祭典是米貢祭，米貢祭的祭儀程序繁多，依序如下：1.米貢祭前的祈禱。2.第一個儀式－米貢祭開始的祭詞。3.祭司宣布祭典開始。4.懸掛明年的種子tumatim koca ania。5.阻擋惡靈mala anibi。6.詢問夢境 tisune ucepe 問好夢。7.報戰功（tisa'<span style="text-decoration: line-through;">u</span>ku）。8.與族人分享食物。9.唱祭歌。10.眾人歌舞。11.眾人歌舞。12.家族互訪（paleveve）。
