      <strong>一、展演活動</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從數量上來看，排灣族和魯凱族的展演活動是一覽表內最多的，從這裡可以得知排灣族和魯凱族在展演活動上的活躍程度。兩個族群的展演活動比較重要的有「森之藝－森林藝術祭」（林後四林平地森林園區演出）、「2019 臺灣燈會在屏東」（屏東縣大鵬灣燈區演出）、「來唱歌吧！泰武古謠傳唱－唱進WOMAD 行前首發場」（衛武營國家藝術文化中心音樂廳演出）、「驚嘆樂舞－臺灣原住民樂舞饗宴」（國父紀念館演出）、「『天籟之音～媽媽教我的歌』全國師生鄉土歌謠比賽特優學校音樂會」（國家音樂廳演出）、「口鼻笛大師傳承展演－在氣息與指尖的相遇」（臺灣原住民族文化園區娜麓灣歌舞劇場演出）、「2019 驚嘆樂舞臺南成功場－祈福」（國立成功大學成功廳演出）、「玉山vs大武山的傳說」（屏東演藝廳演出）、「音樂瞬間－2019 姜宜君中提琴獨奏會」（屏東演藝廳演出）、「2019卡列拉斯・希望原鄉」（屏東演藝廳演出）、「斜坡上的藝術節」（潮州林後四林平地森林園區）。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;相對於上述兩族，規模較小的族群出現的展演記錄，讓我們眼睛為之一亮，這些族群像是大武壠族、西拉雅族、噶哈巫族、道卡斯族等，非常難得。大武壠族的展演活動有「大武壠族回家跳舞」（臺中國家歌劇院中劇院演出）、「展高雄：迴山歸鄉－六龜與杉林特展」（高雄市立歷史博物館第一、第二展覽室）、「第五屆大武壠歌舞文化節」（日光小林社區演出）等；西拉雅族的展演活動有「第八屆臺南市西拉雅文化節主題Makitila ki ririma ki Siraya 手牽西拉雅」（臺南大遠百成功店前廣場演出）；噶哈巫族的展演活動有「臺中世界花卉博覽會演出」（臺中世界花卉博覽會園區演出）；道卡斯族的展演活動有「臺中世界花卉博覽會演出」（臺中世界花卉博覽會園區演出）等。</p>
