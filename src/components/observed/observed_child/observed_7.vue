    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;拉阿魯哇族（Hla'alua）本年度共有17個活動，分布在不同的類型。此族的活動雖不若排灣族魯凱族那麼多，但是若以全族不到四百人這樣的族群規模來看，我們覺得這個族群幾乎是傾全族之力來做文化，非常不容易。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;拉阿魯哇族（Hla'alua）是2014年6月26日官方認定的第十五個臺灣原住民族。拉阿魯哇族早期被歸類為鄒族的一支，和卡那卡那富族被稱為「南鄒」，2014年正名為拉阿魯哇族，主要分布於現今高雄市桃源區。現存有三個部落，分別為「排剪社」（paiciana）、「美壠社」（vilangan<span style="text-decoration: line-through;">u</span>）、「雁爾社」（hlihlara），另有部分族人居住於那瑪夏區瑪雅里一帶。拉阿魯哇族是一個僅約四百人的族群，人口雖少，但是活動卻非常旺盛，每年的「聖貝祭」幾乎已成例行性的祭典。有關於聖貝祭的由來如下：
    </p>
    
    <p class="bui">
      遠古時代洪水退散後，所有的人類包括拉阿魯哇族的祖先都分散了，他們隨著洪水的消退往下遷移，他們往東方走，走到某個地方碰到小矮人的族群，小矮人們接受了拉阿魯哇族人，於是雙方就住在一起。在相處的這一段時間當中，小矮人教了拉阿魯哇族人很多農事的技巧，指導的項目當中，也包含每年一月舉辦的聖貝祭。一段時間之後，拉阿魯哇族人越來越多，不想再麻煩小矮人，決定離開hlas<span style="text-decoration: line-through;">u</span>nga這個地方，小矮人捨不得拉阿魯哇族人離開，因此離開的時候，把他們最重要的寶物（貝殼）送給拉阿魯哇族族人，並且叮嚀要把此寶物當作自己的神，拉阿魯哇族人按照小矮人的指示，每年舉行聖貝祭。
      <sup>1</sup>
    </p>
    
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本次祭典在兩個地方舉行，2019年2月28日在雁爾社桃源里四社部落，活動內容有1.迎神祭儀（mariatara'<span style="text-decoration: line-through;">u</span>hla）2.敘述遷移史（umai a pusasaruan）3.接風（makuakua）4.開甕（miavavutau）5.請出貝神6.主祭嚐肉（mai'ili）7.男眾嚐肉（paruacuru）8.眾人喝禮酒（pialangahlu）9.灌祭（pitatamu）10.刺豬祭（papacitaruramu）11.請出貝神（makuaku）12.歌舞祭神（musurauvu）13.驅趕惡靈祭（paria ihlicu）14.眾人唱祭歌。2019年3月1日，美壠社及排剪社在高中里美蘭部落舉行，活動內容有：1.迎神祭儀（mariatara'<span style="text-decoration: line-through;">u</span>hla）2.敘述遷移史（umai ap<span style="text-decoration: line-through;">u</span>ngahla sasaruana）3.灌祭（pialangahl<span style="text-decoration: line-through;">u</span>）4.儀式前的準備工作5.邀請頭目參與祭典6.升起祭火（puasipi）7.到各家取酒與取薪材8.主祭嚐肉（mai'ili）9.男眾嚐肉（paruacur<span style="text-decoration: line-through;">u</span>）10.眾人喝禮酒（pialangahl<span style="text-decoration: line-through;">u</span>）11.祈求獵物鑽木取火（makinikuli'i）12.眾人歌唱13.刺豬祭（papacitaruram<span style="text-decoration: line-through;">u</span>）14.邀請聖貝（makuaku）15.開甕（miavav<span style="text-decoration: line-through;">u</span>ta<span style="text-decoration: line-through;">u</span>）16.丟聖貝17.眾人歌唱18.慰勞祭（tumahliasasangar<span style="text-decoration: line-through;">u</span>）19.團獵20.首級的安置21.驅趕惡靈祭（paria'i ihlicu）22.團獵祭（muakamuhlu）。
