    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;從上面的資料可以整理出幾個重要訊息，首先，名稱上，部落族人慢慢擺脫「五年祭」這個以往大部分學者的稱呼，而改成「maljeveq」這個族語名稱，或是「祖靈祭」這個強調神的身分的稱呼；同樣的情形，「六年祭」這個名稱也很早被部落族人改成「pusau」或是「送祖靈祭」等的稱呼，顯示族人已經開始意識到，以往學者的稱呼，到了這個時代似乎已有改變的必要，於是，各部落紛紛朝向「切合祭儀精神」這樣的目標修改，依此要領，「maljeveq」、「pusau」、「祖靈祭」、「送祖靈祭」等稱呼，就比較能被族人接受。日據時代，日本人因為限制原住民族辦理五年祭，改朝換代以後，各部落紛紛復振此祭典，最早恢復辦理的古樓以及土坂部落，至今已持續辦理了很長一段時間。觀察今年的紀錄，我們看到祖靈祭以及送祖靈祭主辦的單位，已達8個部落，可見，復振祖靈祭之火，已在各部落蔓延。最後須提及的，是祖靈祭的執行牽涉到的是宗教上的議題，傳統的宗教是由pulingau（巫師）與神溝通，這對於幾十年來，部落事務習慣以西方宗教儀式執行的族人或是神職人員來說，勢必將成為一個必須面臨的問題，而這個議題亦將考驗著年輕一代的族人。</p>
    <p>
      <strong>三、推廣教育</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本項紀錄，以排灣族、魯凱族、拉阿魯哇族、卡那卡那富族的活動較多，平埔族此部分並沒有留下紀錄（其他活動其實亦有推廣的意涵）。此項紀錄值得提及的是廣播電臺的音樂推廣，高雄廣播電臺FM94.3，常常製作原住民語言與音樂之相關節目，1月份電臺邀請了魯凱族族人，在「Ya！原來是這樣」的族語廣播節目上，呈現魯凱族的音樂。3月9日及23日，電臺邀請了拉阿魯哇族族人，在「Ya！原來是這樣」族語廣播節目中，推廣拉阿魯哇族的歌謠。高雄廣播電臺以外，國立教育廣播電臺高雄分臺，於5月17日，邀請排灣族人分享該族音樂。2017年開播的Alian FM 96.3原住民族廣播電台，許多節目曾經播放原住民16族的音樂，徐睿楷主持的「原聲探索」就是一例。</p>
    <p>
      <strong>四、傳習活動</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;傳習活動的實施大致上有兩種活動類型，一種是以部落的某個學習空間，進行語言或古謠的學習，另一種是結合動態的活動，所做的傳習活動。第一種的傳習方式，像是排灣族「來義鄉108年度原住民族語言推廣課程（含歌謠）」（新來義活動中心小型會議室）、大武壠族「大武壠族古謠傳唱培訓與展演」（日光小林社區發展協會）、排灣族「南排灣族語及外麻里巴社歌謠課程－kai ni vuvu」（牡丹鄉東源分校課輔班教室）、排灣族「2019『文化資產．大師傳承』鼻笛傳習工作坊」（安坡部落教室）、排灣族「排灣族鼻笛資源手冊及教師研習與學生體驗工坊」（屏東縣來義高中專科教室）。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另一種結合各地的活動所做的文化傳習，像是排灣族「Kapanan部落文化音樂小節－讓你的回來祝福」（牡丹鄉石門國小）、排灣族與魯凱族「2019南島族群婚禮情牽大武山愛戀Masilid－開幕活動」（屏東車站大武山星空廣場）、排灣族與魯凱族「2019南島族群婚禮情牽大武山愛戀Masilid－新人報到暨新人之夜」（北葉活動中心）、魯凱族「108年達魯瑪克maisahoro 活動」（Belenga）、排灣族「牡丹社事件145周年紀念活動」（牡丹社事件紀念公園文化廣場）、卡那卡那富族「河祭」（達卡努瓦溪）、排灣族「屏東縣三地門鄉第三屆原住民族語歌謠觀摩會暨母親節表揚活動」（口社部落活動中心）、排灣族與魯凱族「2019屏東縣原住民族收穫節」（屏東縣立田徑場）、排灣族「聽VUVU在唱歌－高士部落傳統古謠紀錄與保存計畫」（高士部落集會所）、排灣族「108年重要傳統表演藝術及重要傳統工藝傳習成果展演」（文化部文化資產園區衡道堂）。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;以上兩種傳習方式，不論是找某個學習空間，進行語言或古謠的學習，或是結合動態的活動，所做的傳習活動，兩種方式都讓文化的傳習得以落實，並獲得最好的成果。</p>
    <p>
      <strong>五、國際與兩岸交流</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;國際與兩岸交流的紀錄不多，全部都集中在排灣族與魯凱族兩個族群，活動大致分成兩個類型，第一種是音樂團體到國外或在國內參加音樂活動，如排灣族「WOMADelaide音樂節－泰武古謠傳唱」（ADELAIDE, AUSTRALIA）；排灣族「2019夏威夷Honolulu藝術季」（Hawaii Convention Center stage）；排灣族「溫哥華國際舞蹈藝術節Vancouver International Dance Festival－蒂摩爾古薪舞集」（Playhouse, Vancouver）；排灣族「原音跨界暨泉州師範學院－屏東大學兩校合唱交流之夜」（屏東大學林森校區表演廳）。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第二種則是音樂團體參加國外的音樂競賽，如排灣族「屏東希望兒童合唱團於「2019勃拉姆斯國際合唱音樂節暨合唱比賽」得獎」（德國）；魯凱族「屏東青葉國小合唱團於「2019維也納國際合唱比賽暨音樂節」獲得金獎第1 名」（奧地利－國立維也納音樂暨表演藝術大學、維也納皇宮、布拉提斯拉瓦皇宮、布拉格皇宮）等。參加國外音樂競賽的團體，其年齡層為國小階段（希望兒童合唱團以及屏東青葉國小），能夠在歐洲的競賽場合得到獎項非常難得，政府單位應該要為這些資賦優異的兒童以及學校，提供更多的發展空間。</p>
    <p>
      <strong>六、研究與出版</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;全年的研究出版記錄僅四筆，排灣族兩筆，魯凱族兩筆。排灣族的紀錄為排灣族「刻劃排灣學－文化的持續與變遷論文分享會」（草埔國小視聽教室）；排灣族「流浪的NALUWAN」（靜宜大學南島民族研究中心）。魯凱族的紀錄為魯凱族「2019 魯凱學研討會」（霧臺鄉公所）；魯凱族「流浪的NALUWAN」（靜宜大學南島民族研究中心）；排灣族魯凱族「《臺灣高砂族之音樂》出版」（國立傳統藝術中心臺灣音樂館出版）。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;前面四筆為研討會以及專題講座，顯示越來越多以文化為主軸的研討會，音樂是必然會出現的議題，有了音樂議題的設置，就會邀請民族音樂學者提出論述。《臺灣高砂族之音樂》出版這件事是2019年極為重要的紀錄，原著為黑澤隆朝，他於1945之前，記錄臺灣島各原住民族的習性，並實地踏查記錄，觀察歌謠的特質，並將歌曲做成曲譜，之後也進行器樂與樂器之調查。《臺灣高砂族之音樂》主要內容分成三部分：第一是對臺灣原住民族的文化以及歌謠舞蹈進行介紹，第二是描述原住民族所使用之器樂，並對原住民所使用的樂器進行研究，第三為黑澤隆朝教授之田野日記。</p>
    <p>
      <strong>七、文化政策</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;文化政策總數量為15筆，排灣族、魯凱族、拉阿魯哇族、卡那卡那富族佔8筆，而平埔族佔7筆。全部紀錄包含：原住民族委員會所頒布－原住民族委員會公告新增原住民族傳統智慧創作11件；行政院原住民族委員會所頒布－原住民族委員會通過及公布拉阿魯哇族傳統歌謠傳統智慧創作專用權；行政院原住民族委員會所頒布－原住民族委員會通過及公布魯凱族傳統歌謠傳統智慧創作專用權；原住民族委員會原住民族文化發展中心「108年度培育原住民族藝術人才補助計畫」；財團法人原住民族文化事業基金會「108年度第1期原住民族文化藝術補助結果公告」。</p>
    <p>
      <strong>八、多元活動</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;多元活動僅一筆，為排灣族、魯凱族、卡那卡那富族、拉阿魯哇族「《臺灣高砂族之音樂》新書發表會」（臺灣原住民族圖書資訊中心）。《臺灣高砂族之音樂》是由黑澤隆朝原著，王櫻芬主編，王櫻芬、劉麟玉、許夏珮譯註，國立傳統藝術中心臺灣音樂館以及南天書局合作出版。</p>
    
