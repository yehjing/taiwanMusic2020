    <h3>參、人物</h3>
    <p><strong>一、離世</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年2月3日，泰雅口述傳統保存者黃榮泉先生辭世。黃榮泉先生除為族群第一代接受高等教育者，音樂文化上，對於傳統口述歷史的記憶，以及傳統遷徙史詩Lmuhuw的吟唱傳承，都卓有貢獻，是桃園市政府登錄的文化資產「傳統藝術」類「泰雅口述傳統」技藝保存者。以88歲的高齡過世，令人惋惜，原民會副主委前往公祭，代表總統頒贈褒揚令。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;4月7日，另一位與本子計畫有關的阿美族地方文史工作者黃貴潮先生也過世。黃貴潮先生早期曾參與鈴鈴唱片與心心唱片的製作，對於原住民音樂的流行化有深刻的認識。許多臺灣民族音樂學者到臺東地區進行原住民相關研究，都會去拜訪黃貴潮先生。此外，黃貴潮本身雖然行動不便，但卻以無比的意志力，多方透過訪談、照相、錄音、錄影等方式，收集阿美族的各部落文化事務，且其勤於筆耕，能將收集之資料集結成書，為部落與族群留下許多珍貴的資料。公祭那天，臺東縣長亦到現場祭弔這位讓人尊敬，有堅毅不撓的意志力以及深刻人文關懷的文化工作者。
    </p>
    <p><strong>二、頌揚</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;泰雅族的Lmuhuw指相互穿梭對話，是以口述方式敘述族群神話傳說、祖訓規範、民族緣起、族群移動遷徙等內容，表達方式含蓄內斂，充滿隱喻，表現族群深厚的文學與哲學內涵。而以口述來吟唱，更是泰雅族語言智慧的結晶，是族人異常珍貴的無形文化資產。Lmuhuw
      na
      Msbtunux（泰雅族大嵙崁群口述傳統）在2019年，被登錄為我國第一項國家級的「重要口述傳統」文化資產，且在2019年的最後一天，文化部長鄭麗君親自桃園市復興區，將認定證書頒發給這臺灣第一位口述傳統保存者，泰雅族的「人間國寶」WatanTanga林明福耆老。WatanTanga林明福可說是「現今桃園地區唯一能清晰傳唱泰雅古語的耆老」（https://www.moc.gov.tw/information_250_107462.html），2012年已被文化部指定為重要傳統表演藝術「泰雅史詩吟唱」保存者，在此期間，更帶領六位藝生，進行傳承，並與藝生和19位耆老，共同完成《Lmuhuw語典：泰雅族口述傳統重要語彙匯編》。此次的登錄公告以及認定理由，以泰雅族語和漢語「雙語併行」方式呈現，顯示國家對於原住民文化的尊重。
    </p>
    <p><strong>三、緬懷</strong></p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年3月有著幾場紀守常神父百歲冥誕的追思活動。屬白冷外方傳教會的瑞士籍神父紀守常，於1954年來到臺灣，先至臺東傳教，同年7月至蘭嶼。紀神父在蘭嶼的16年時間，不論教友與否，均盡力接濟協助，另外讓兒童普遍能受教育，婦女能受職業訓練，並為了雅美（達悟）族的政治權益，不惜站出來，與當時國民政府力爭，但卻不幸於1970年車禍而死。族人對其感恩至深，在其百歲冥誕之時，出版了《飛魚之島
      達悟之父紀守常》繪本；舉辦了攝影展（3/23-7/31）；以及在臺東市（3/16）及蘭嶼朗島（3/24）兩處，舉辦了感恩彌撒和紀念音樂會。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;9月12日臺東馬蘭部落為頭目Kakitaan Kolas
      Mahengheng舉行了167歲的誕辰紀念活動。馬蘭大頭目郭拉斯．馬亨亨，清末至日據時期的人，是馬蘭部落遷社以後，首位擔任頭目者，馬蘭阿美族人為了紀念這位曾帶領族人開墾，並透過種種談判，使族人免於受他族欺侮的頭目，這一天由其後代郭子雄先生，口述先祖馬亨亨的種種事蹟，並由杵音文化藝術團演唱馬蘭部落傳統的複音歌謠。
    </p>
    
    <h3><strong>肆、政策與推廣</strong></h3>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;原住民對於自身傳統文化的保護與詮釋意識更加強烈，今年本計畫的族群中，有四個項目通過原住民傳統智慧創作權認定，其分別是阿美族大馬蘭部落的sapilekal
      no pakarongay a
      radiw（少年報訊曲）（2/25）；卑南族下賓朗部落申請的婦女除草祭：misa'ur、muhamut（6/21）；阿美族迦納納部落的ilisin：limorak儀式祭歌（7/26）；O
      Sakilikoda a Radiw(ki'eciw) no Fata'an
      －馬太鞍部落的豐年祭樂舞14首傳統祭儀歌謠/歌曲。原住民的傳統智慧創作權，因此受到更多的保護。但在受保護的同時，這些音樂的推廣，也受到許多的限制，外人除了演出時需要得到族群的認可，有時族人也會將自我意圖加諸外人與表演者。這與文化部文化資產局所推動的無形文化資產保護及推廣形式，有著很大的差異。因而12月7日在「臺灣新音樂的歷史見證」學術研討會中，有曾毓芬所發表的〈臺灣無形文化資產保護現況下的原住民音樂創作議題探討：賽德克族祭典舞蹈歌Uyas
      Kmeki之個案研究〉論文，就是探討這方面的問題。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;直屬文化部的國立臺東生活美學館發起的「花東多元音樂扶植計畫」。此計畫為從2016年至2021年的六年計畫。2019年此計畫的人才培育部分，共成立了四個歌謠班，與本子計畫族群有關的，共計有兩項成果，分別是跨五個卑南族部落（利嘉、下賓朗、泰安、寶桑、南王）的「到部落學歌謠－卑南歌謠班」（7/18成立），與阿美族馬蘭部落的「到部落學歌謠－阿美複音青春歌謠班」（8/6成立）。兩個族群的歌謠班，加上另外兩個非本子計畫下的巴喜告部落的布農青銀歌謠班，與土坂部落的排灣族歌謠班，同時在11月9日於臺東鐵花村舉辦了成果發表會。此外，阿美族馬蘭部落的杵音文化藝術團，亦針對國小孩童有「唱我們的歌
      說我們的故事」複音傳承計畫。由於阿美族複音歌謠音域較大，孩童聲帶尚未發展完善，無法演唱這麼大的音域，因此從2017年開始實施，但效果並不明顯。2019年針對國小孩童，改以單音歌謠教學，複音歌謠則主要是欣賞，希望孩童能理解部落歌謠特色，並透過單音歌謠教學，以為日後複音歌謠吟唱打下基礎。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;與此同時，達悟（雅美）族也有兩項歌謠傳承的訓練班。4月15日開始，每週四次，總共13次的古謠研習班「古謠聚會所開課」，參與者學習了傳統的古調與舞蹈歌謠。達悟族歌謠注重創詞，學習者先學會了解歌詞的意思，以及其文化背景，再透過背誦這些歌詞，逐漸進入學習狀況。5月7日，課程並進行了成果發表。另外，由財團法人基督教蘭恩文教基金會所主辦的「達悟歌謠吟唱班─聽故事、說母語、學歌謠」（10/8-29），利用10月四週的每個週二，學習了十人大船下水歌謠、地下屋落成歌謠、頭髮舞歌謠古調、豐碩之地（小蘭嶼歌謠）、船上垂釣歌謠、釣鬼頭刀歌謠、搗小米歌以及Panlanlag祝福歌謠等曲調。其中許多歌謠，是今年6月22日，島上族人受邀至臺灣戲曲中心小表演廳進行演出時，所集體習得之歌謠，在此並將其傳播給其他族人。
    </p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;至於泰雅族以及賽夏族的歌謠傳習，主要在學校或合唱團中進行。例如2019年有相關新聞的泰雅族嘉興國小與錦屏國小合唱團、泰雅學堂合唱團體；或如賽夏族2019年有活動的東河國小。可見不同族群的傳承方式，受其族群個性、族群分布、與族群間的人與人之互動形式，展現了差異性。
    </p>
    
