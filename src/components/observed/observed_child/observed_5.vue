    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由國立傳統藝術中心臺灣音樂館所主辦，於臺灣戲曲中心進行的原住民音樂會，從2017年開始，已舉辦多場。2019年有邵族、雅美（達悟）族以及拉阿魯哇族的三場音樂會。與本子計畫有關的，是雅美（達悟）族的音樂會。舉辦時間為6月22日，是主辦單位整個「世代之聲－臺灣族群音樂紀實系列」的第20場演出。音樂會名稱為「傾訴達悟－大海的搖籃曲」，是由蘭嶼整個島嶼優秀歌者所組成的，除了成年歌者17位，另有椰油村椰油國小12位學生的參加，由本子計畫主持人進行策展。活動分上下半場，除了前導影片，上半場以男性歌謠為主，下半場則為女性與兒童歌謠，最後結束在划船與杵小米時所唱的〈用力的歌〉（vaci）。此場音樂會，貴賓雲集，文化部蕭次長與教育部范次長均光臨欣賞。族人穿著傳統服飾，演唱了15首傳統曲調以及6首新創曲調。其中新創曲調為現代創作，運用當今的旋律和聲與律動，與傳統歌謠形成很大差別，是兒童所演唱的形式。此外兒童也配合成人，在歡慶祈福（manwaway），頭髮舞、蚯蚓舞、以及用力的歌部分，以身體動作，參與成人的歌謠，顯示了族群音樂的傳承。除此之外，此次演唱展現了外界難得聽聞的〈四門房之歌〉。歌謠旋律緩慢悠揚，配合幻燈投影以及這次的共同策展人，也是族人的領隊：夏曼．藍波安這位海洋文學家，在練唱過程中的音樂隨筆散文，增添了整場音樂會的詩意。另對於部落族人而言，本次的演出，不似以往族人在外地的演出，都僅僅演唱一兩首曲子，歌者人數也較為侷限。本場演出，是族群第一次在外地，以音樂會的形式，在同一時間段之內，演唱如此多樣的、不同旋律的樂曲，且整場音樂會有一個半小時的長度，對於族人而言，可說是一次非常大的挑戰，並且也獲得了很大的成功。
    </p>
