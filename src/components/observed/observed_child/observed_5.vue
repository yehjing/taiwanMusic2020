      Taipei傳音樂展演空間，舉辦了一場個人的懷舊演唱會。盧靜子1960年代發跡，以母語演唱流行歌曲，許多取材自傳統旋律，加以歌聲優美，曾紅遍全臺。鈴鈴唱片等多家唱片公司，曾幫其發行許多膾炙人口的歌曲，包括〈馬蘭姑娘〉、〈阿美三鳳〉等。演唱會上，原住民委員會頒發了原住民族二等專業獎章給歌手盧靜子，且野火樂集也在之後出版了其12首歌謠CD以及紀錄片DVD。
