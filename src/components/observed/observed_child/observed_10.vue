    <h3>
      <strong>結語</strong>
    </h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年臺灣的道法教活動與祭儀，在莊嚴與眾多的熱鬧喧嘩之中，連結了宮廟、信徒、選舉、選票與兩岸的各種議題。宮廟與信徒掌握了臺灣大小選舉的選票，間接地所選出的代言人，也將引導臺灣前途的走向；所以各大宮廟或其代言人，以及前來拉票的各政黨候選人，也在各大法會舉行期間，無不賣力亮相，爭取曝光度與影響度；特別是本年度所蹲點的樹林濟安宮東遷百年五朝清醮法會，各政黨高層級的候選人與宮廟負責人一同出面、亮相或站臺，讓社會大眾引起聯想，話題佔盡各式媒體版面與空間。此外還有在竹圍淡水河畔舉行三天的「淡水河河祭路祭法會」，因這次負責的執行單位「宗教聯盟黨」，是由多個不同宗教團體所組成的新政黨，成員眾多，但以三大宗教團體－佛、道與藏信徒為主的政黨；所以此次的祭祀祈福法會，也設有此三教各自的法壇，一改之前十多年來，只由佛教團體擔綱的佛教祈福法會；這或許這可以理解臺灣選舉與宗教結合的一大特色與緊密關係，造成有些挾著眾多信徒的宮廟負責人，是政治人物想積極拉攏的對象。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣社會對宗教信仰的高度熱誠，不僅反應在選舉年、宮廟的各種年度法會、盛會，民間日常生活的習俗，也處處充滿了與神同在、神明啟示的常民現象。自約1950年代以來，臺灣民間信仰逐漸興起了「母娘」信仰的風潮。「母娘」也有一稱「五母」、「五申大慈尊」（或「無極五母大慈尊」），如本年度所記錄的「瑤聖宮」宮主張女士所信仰與賴以服務的對象。「五母」是源自母娘所建構出來的五母神話，即東王無極聖母、南王地母至尊、中王無極老母、西王瑤池金母、北王無生老母等。據學者鄭志明（2010）的長期觀察，母娘信仰是一種開放性的宗教形態，重視顯靈的神聖交感信仰經驗，經由附身的靈媒傳達其下降人間救世度人的宏願，再經由神聖顯化的靈驗，吸引信眾皈依於王母名下為契子女。經過這幾十年地方民間宗教發展旺盛的年代，依附在母娘信仰下的靈媒、靈乩，形成一股集體性的靈修方式，藉由所開發出來的通靈方式，從求助者轉為助人者，甚至發展出集體會靈的大型法事，稱為「會靈山」，帶領信眾到各地的靈山道場交會母娘歸元的靈脈，認為每一個人的靈性可以接受來自靈界的訊息；或在母娘的幫助下消災解厄、醫治百病。在這種日趨明顯的，由靈乩、靈媒所發展出來的宮廟，或許將逐漸發展出新型的宮廟法會，而傳統的道士團，或將會面臨更多的道教科儀增補、修訂的情況。如本報告所記錄的清微道派的護國消災祈安植福法會，就是一個受到靈乩宮主的委託，以道教兼具靈山的氛圍所進行的法會性質，這也是臺灣目前其他道教法會所少見的場景。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;綜觀本年度的各道法派宗教活動，多元而活潑，繽紛中有序；各道派或法派之間互相觀看、學習或激盪出新的火花，或間接調整自己的走向或風格；這些都因為臺灣社會相對開放的宗教環境、相互尊重的宗教信仰、與信徒的包容性有關，在自我尋找宗教心靈的歸宿中，也不忘了理性的尊重別人的信仰；所以我們看到三教可以合一的共同舉辦一個超渡的法會，也可以看到女神三奶夫人，如何被道教或法教分別以祂的名義，延伸出各取所需的儀式。對常民信眾而言，不管哪尊神，只要祂保佑，有拜就有保佑，這是最大的公約數。</p>
