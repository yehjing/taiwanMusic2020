    <p>
      <strong>（二）佛道藏三教合一的「全民祈福百年安康淡水河河祭路祭大法會」</strong>
    </p>
    <p></p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2006年起，由淡水竹林慈玄宮結合地方團體及里長於每年農曆7月由佛教團體在淡水河畔的關渡碼頭、竹圍碼頭等多次舉辦「淡水河河祭、路祭大法會」，進行超渡無祀先靈之法會，至今已有十八多年歷史。今年，2019年首次由「宗教聯盟」所執行的法會，負責邀請多個不同宗教團體參與三天的祈福大法會：佛教團體、藏傳佛教團體、道教團體、法教團體、一貫道團體、基督教團體等。其中的佛教團體則在18多年前即開始參與的唯一團體，今年由「宗教聯盟」主辦，擴大邀請其他類別的宗教團體會員參與。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;據主辦單位表示，淡水地區從關渡到淡水、三芝交界的重要道路，交通事故頻傳，為超渡亡靈及各路好兄弟，每年舉辦法會請法師誦經、舉行科儀超渡，今年更擴大邀請天主教、基督教、藏傳佛教、一貫道及軒轅教等多個團體共同參與祈福、誦經持咒，讓過往先祖、淡水河亡靈聽經聞法，在不同教派的加持下離苦得樂。法會期間的儀式空間，主要以佛教祭壇最為盛大，是主壇場；其次為藏傳佛教壇場，再其次則為直接面對淡水河的小壇場，由道教與法教共用的道法壇；至於其他的教別，如基督教、一貫道、軒轅教則在第一天早上分別派出代表，與佛教團體、藏傳佛教及道法教代表、里民代表共同搭船多艘，於淡水河上參與舉行境外灑淨的河祭路祭儀式，會場中並未設壇祭拜。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在第一天上午的佛教的法會，在淡水河上以船祭灑淨的方式完成後，舉行午供；下午兩點到五點期間，由道長陳瑞南（1950-，法號玄玄金元龍），同時也是「中華世界大同龍華聖道會」第三屆理事長，負責在道法壇以另類的道、法教儀式進行。陳道長於1981年（民70）前後與當今【真佛宗】創教教主－蓮生活佛盧勝彥同時在總持寺隨普方金剛上師習法。陳理事長的儀式過程與內容，涵蓋佛教、密教、道教與法教。第一天上午陳道長首先在道壇的儀式空間，進行淨壇（淨化）的動作，意在把不要的靈請走，把想邀請的神靈請進來；接著進行安靈的動作（請本尊法相之靈）；開九皇九星君燈（含七星君靈）。接著當天下午之法會，以二至三小時的時程，進行誦唱【爐香讚】、【清淨讚】；密宗的【清淨咒】、【召請咒】；道教方面，邀請無極諸聖尊、一炁老聖祖祖、茅山宗歷代祖師、全真教歷代祖師、張天師歷代祖師爺與護法神、臺灣蓬萊仙島佛寺宮廟諸神菩薩、圓山忠烈祠天兵天將等，再透過觀想、念佛咒、結手印、念密咒、施洒甘露法水等；最後念誦道教的【玉皇赦冤咒】，佛教的【往生淨土神咒】等等的繁雜念誦經文程序，全由陳道長一人擔綱，內容涵蓋佛、道、密等各方咒語、真言、手印來回交互應用，以期達到法事的圓滿，使河路、陸路無主靈魂得以早日解脫，離苦得樂。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;第二天上午陳道長則進行開光點眼的儀式，下午與靈山弟子互動，開點光明燈等儀式動作；第三天上午，原則上比照第一天，下午召請上岸的萬靈參與普渡法會；最後法會結束，收盤歸位等儀式。被超渡的萬靈在享用普渡施食後，將隨法船駛離、各奔前程，往西方極樂世界等，不再滯留於人世間。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;陳道長約在九年前開始活躍於宗教界，多年來並收有弟子多人，分別來自靈山派及其他派別的宗教人士。此次的法會，透過宗教聯盟邀請，以【中華世界大同龍華聖道會】理事長的身分參與主持法會祭祀活動。陳道長法事吿一段落時，則由蔡信平道長兼法師（1961-）進行一些道、法教儀式的程序的互用。蔡道長第一天上午隨者主辦單位代表、里民代表及宗教團體代表，以法師的身分，腰部繫帶白色龍虎群，帶著柄鼓與鑼與眾人登上主辦單位所租借的船隻，並在淡水河水上、岸邊，備以法器，開啟地獄門、水路門、陸路門、天門、碼頭門等動作，召請亡靈上岸。當亡靈上岸後，恭請沐浴，在盥洗之後，聚集於水車藏（狀）（或稱法輪）之下，聞法聽經，並享用祭品。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在法會的最後一天下午，蔡道長進行登壇普施，約於五點鐘左右，各宗教團體，輪流誦經請亡靈趕快登上即將駛離的法船。在一陣陣的鞭炮催響聲中，法船開始燃燒，逐漸駛離碼頭岸邊，在一片煙霧中消失於水上。蔡道長（道號圓平；法號明佑），也是一位很另類的道長兼法師，其彰化住家有自設的宮廟（「華壇福春宮」），目前也是宗教聯盟成員之一。據蔡道長言，他於2010年與30多位師兄弟在自家宮廟同時皈依來自河南的全真道長石永田（為河南鹿邑縣第18代全真道士，目前約80多歲）。當時拜師的一批師兄弟，有些人後來自行去大陸學習科儀，如來自嘉義民雄的何金鳳道長，可能是唯一前往河南學習全真道的一位。大部份的成員只行拜師禮，並未學習道教科儀部分，因為老師沒教，也沒有進一步的學習規劃。至於法教儀式部分，據蔡道長自言，很多是自己學習而來。所以這三天的道法教儀式，並沒有一般所常見的有前場道士與後場樂師的道士團，而是兩位個人道士分別獨立「作業」的儀式展演。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三天的法會期間，主辦單位準備了數量頗多的水燈與兩艘法船：【西方船】與【祈福船】，並在第一、二天晚上放很多小水燈於淡水河上；第三天黃昏時則將兩艘法船（內放置大量紙錢、飲料）移放置於河上，在等待眾靈登船之後，於鞭炮聲中燃燒於水上，開往西方極樂世界。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法會期間有多位政治人物，如前立法院長王金平、新北市長侯友宜、立法委員等都到場參拜祈福。此外主辦單位在三天法會發放一千五百個孝親平安袋，袋內書籤印有《孝經》孝行語錄，寓意農曆7月是佛教的「教孝月」。法會過程有誦經、恭送祈福船及送聖儀式，藉以超渡亡魂與先靈。</p>
