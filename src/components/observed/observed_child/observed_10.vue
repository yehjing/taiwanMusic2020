    <p class="firstRow"></p>
    <h3>前言</h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度蹲點觀察的宗教展演與儀式團隊，包括了傳統的道教與法教團體，如北部的正一道派、南部的正一靈寶道派、北部的普唵小法、法主公派、南部的閭山小法等；同時在新興道教派別中，也包括了全真、禪和與清微道派儀式的觀察。筆者觀察到新興道派之傳統宗教儀式中，加入了當前社會宗教信仰多元的需求，出現的一些儀式上「改良式」的微調變化，一再體會到傳統與變遷緊密關係的意涵。宗教儀式執行者因應社會民間宗教信仰的時代需求，不得不自我調整，在傳統儀式中加入新的元素，讓參與儀式的主、客方，在一場期待的儀式中，得到宗教的法喜、包容與祥和。這些的演變，回歸到信徒對宗教包容性的肯定。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧這20-30年來，各個不同宗教族群的領袖，分別提倡尊重對方所選擇的宗教信仰，才是回歸到宗教信仰的本質。在當前臺灣宗教信仰自由的環境中，信徒具有強烈的宗教信仰熱誠，但又能理性地尊重其他信徒的宗教選擇，這也是臺灣宗教信仰百花齊放的助力之一。除此之外，多年來大專院校成立宗教系所，透過知性與理性的學術探討與養成，宗教一層層神秘的面紗逐漸被揭露，回歸到人類基本心理需求面向的探索，同時對各宗教的儀式、文化與社會互動關係的瞭解，有更多的認識；另一方面愈多年青人在透過接納祖傳宗教業務的同時，也進入宗教系所進修，取得碩士、博士學位，讓道教、法教信仰不再只停留於民間鄉民的「無知」刻板印象中。這些具有祖傳實務基礎的展演能力，同時具有論述能力的年青宗教學者，對當今道、法教信仰圈，逐漸透明化與被正確的理解有很大推力。本年度的報告，筆者整理了的幾個儀式特點，並加以說明。</p>
    <h3>
      壹、本年度活動的觀察與評介
    </h3>
    <p>
      <strong>一、學術界道、法宗教的研討會</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近年來多數的宗教系所、學術團體或少數民間宮廟的努力或兩者之間的合作，每年定期舉辦學術研討會，讓學術界的宗教研究更為蓬勃，如全國性的宗教學術團體「台灣宗教學會」，二十年來每年持續舉辦宗教學術研討會，今年以「2019臺灣宗教研究20年的回顧與前瞻 暨 敬天法祖」國際學術研討會，做為一個學術交流的平臺，為學術界的宗教研究，特別是跨宗教與派別的研究，提供了很好的對話契機。</p>
    <p>
      <strong>（一）「台灣宗教學會」國際研討會</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;「台灣宗教學會」是一個由學界、各大專院校宗教系所師生、不同宗教體系的宗教界人士所組成的一個大的學術團體。二十年前第一次開始舉辦研討會，至今每年持續堅持，從未間斷。今年的研討會，比照往年三天的研討會議程規模，分別於中研院民族所多個會議室同時進行，方能容納相關數量的論文。這次二十週年以回顧與前瞻宗教研究，並未設限任何子題，故論文子題多元，包含：宗教立法、敬天法祖、宗教史文本分析、臺灣宗教研究的回顧與前瞻、基督教徒與啟示錄、儀式研究新視野、韓國新宗教田野初探、法教、禪。淨。慈、道教與民間宗教的儀式與音樂、臺韓宗教信仰、儀式音樂的型態與運用、佛教義理的比較研究、基督宗教與在地文化、人間佛教與植物觀的再審視、佛教義理的比較研究等。其中有關道教與儀式音樂的部分，發表的學者有：李秀琴（臺北藝術大學）「戰後臺灣新興道派與道樂之緣起與發展」、李瑞祥（閩南師範大學藝術學院）「祭祀樂舞的現代演繹－以臺北萬民祭天祈福大典為例」、龔道遠（武漢理工大學）「『去天庭掛號』湖北恩施火居道『度職』儀式音樂考察與研究」、史金夢（瀋陽音樂學院）「遼南複州鼓樂在儀式中的應用」。</p>
