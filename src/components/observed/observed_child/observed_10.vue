      <strong>「順天聖母臨水夫人陳靖姑文化節」：「栽花換斗」、「過平安橋」之小法科儀</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;臺灣民間尊稱的「三奶夫人」實指三位女神：大奶陳靖姑、二奶林紗娘（或稱林九娘）及三奶李三娘，即三位神祇的合稱，但有時民間亦單指順天聖母臨水夫人陳靖姑。陳氏為福州市下渡鄉人。臺灣民間信仰所熟悉的順天聖母陳靖姑（767-790），其出生的年代有很多版本，但其歿的日期說法則一致。據傳陳靖姑自幼就聰穎過人，三歲就能通曉經書、持齋禮佛。十三歲時，其拜師閭山派閭山大教主許真君門下，精修道法，十六歲學成歸里。陳靖姑在十八歲奉雙親之命嫁於福建古田縣的劉杞。陳靖姑精通法術，懷孕時剛好遇到福建大旱，在帶孕祈雨時動了胎氣，不幸流產而死，臨終前發願：「死後必成神，專救難產婦女」。據傳陳靖姑、林紗娘、李三娘，三人義結金蘭，一起在閭山上跟隨許遜真人學法，學成後下山傳道，民間因此以三奶夫人為主神及開山祖師的道派稱為三奶派。傳說中的陳靖姑能扶胎救產，保佑護童，對福州民眾有著深遠的影響，隨著移民傳到臺灣，這種習俗形成了一個深厚的臨水夫人或三奶夫人的信仰。該古田縣也是陳靖姑祖廟臨水宮的所在地。陳靖姑死後獲歷代賜封，民間存有多個稱呼，如臨水夫人、順天聖母、臨水陳太后、臨水奶、夫人奶、三奶夫人等；因民間認為三奶夫人祈孕護產保幼極為靈驗，又被尊稱為註生娘娘。</p>
    <p>
      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;近年來隨著臨水夫人在臺灣逐漸增加的信仰人口，主祀陳靖姑的宮廟數目也逐年增加。據統計主祀陈靖姑的宫庙有400多座、配祀宫庙3000多座，信眾超過1000萬人。本年度所記錄的相關法教主辦單位，臺灣「順天聖母協會」於2000年改名為「臺灣順天聖母協會」，迄今計有團體會員約50個宮廟，個人會員550人。從1980年代開始，古田與臺灣以陳靖姑信仰文化為主題的交流聯繫，進入一個新的里程碑。1983年，在臺灣的古田鄉親第一次組團到臨水宮進香，因而在臺灣陳靖姑信眾中產生了極大影響。此後，古田與臺灣陳靖姑信仰文化的交流，從單向往來朝雙向互動，開啟了古田臨水宮組團赴臺考察陳靖姑信仰文化的先例。1999年，應臺灣白河臨水宮邀請，古田臨水宮管委會一行5人首次出訪臺灣，考察陳靖姑信仰傳播情況。2008年，臺灣民俗進香團一行316人首次通過直航方式，從馬祖福澳港直航福建寧德港下白石碼頭，到古田臨水宮祖廟朝拜。2009、2014年，古田縣先後舉辦臨水宮祖廟順天聖母陳靖姑金身巡遊臺灣活動，繞境全島，參拜金身的信眾數十萬人，在臺灣掀起了陳靖姑文化交流的熱潮。同時，每年除夕到正月十五，臨水宮請香接火儀式，臺灣信徒都會集體組團前來祖廟。臺灣信徒也大力捐建祖廟，如祖廟的梳妝樓，是由臺灣弟子、高雄草衙臨水宮住持張宏圖家族捐建。<sup>1</sup>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本年度「2019順天聖母臨水夫人陳靖姑文化節」所舉辦的盛會，已進入第三屆，承續2018年。當天上午的活動從宗教學術研討會之學術論壇在三重珍豪飯店舉行，邀請學者、對岸負責官方代表與廟方代表就「法教三奶在臺灣的貢獻」之論文進行報告；並在三嗇宮同時設有相關民俗活動攤位，如書法家當場揮毫、點燈祈福、迎貴招財、法索繫緣、收驚安魂、斬小人等活動; 另外，當天下午活動的一大亮點是，抬出預先安置於先嗇宮內的古田媽祖神尊來臺巡安繞境，提供信徒鑽轎底的活動。這次的學術論壇除了有部分是學者的身份外，如吳永猛教授、佛光大學宗教研究所所長姚玉霜等幾位；也有古田縣政協港澳臺僑與民宗委主任黃榮輝、福建古田臨水宮祖廟「陳靖姑文化交流協會」副會長余新達與會報告。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;下午的部份進行多種的宗教民俗活動，從14:00-16:00開始進行三奶派祈福科儀活動及普唵法教三奶派大型科儀展演及犒軍儀式。三奶派祈福科儀展演內容有：收驚安魂、法索繫緣、迎貴招財等三項。法教三奶派大型科儀展演內容有：「栽花換斗」、「造橋過限」（過「百花平安橋」）。這兩項活動主要由空中大學吳永猛教授所教導「三重聖傳福音堂」信徒，以澎湖普唵小法儀式進行展演。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;空中大學吳永猛教授，本身也是澎湖普唵派的小法傳承人，這次以「臺灣法教會」名稱，進行「栽花換斗」（求子）科儀的民俗信仰內涵，主要認為女性的元神為一株花或一叢樹；而花苞的顏色分別代表其子嗣之性別，白花為男、紅花為女；透過此儀式，可以請求神明「讓白花先開」，即先生男童，固有「換花」的動作，或不孕的女士可透過此儀式「請花」，請求神明賜子等。至於「造橋過限」即在儀式中搭一座木橋，上圖畫七星狀，經過儀式加持轉化後，該橋變化為神明之平安橋；簡言之，就是一種改運的方式，讓生命中的「關卡」（限）、或災難得以去除，在度過此橋上的關卡後，得以避開災難、解厄。</p>
