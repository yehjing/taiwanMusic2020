    <p></p>
    <p>
      <strong>（一）清微道派「純陽宗壇」主持的「一元復始護國消災星漢普照祈安植福大聖會」「改良式」的微調法會</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;位於新北市三芝鄉的瑤聖宮，於今年10月於三芝區茂長里陳厝坑舉辦為期九天的「一元復始護國消災星漢普照祈安植福大聖會」。此次法會的主辦單位瑤聖宮，約於13年前從臺北市的住宅公寓遷移到現址。宮主張雪娥女士是一位虔誠的母娘信徒，遵奉「無極五母大慈尊」，本身也是靈山乩身，幾十年來透過乩身的問事服務，處理宮廟的大小事，也服務了不少信衆，也讓瑤聖宮不斷地茁壯至今天的規模。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;負責執行九天法會的「純陽宗壇」屬於道派中的清微宗，由道長林元陽所創立，也是道教純陽真宗宗主，嗣漢天師府奏職法師 羅宸，道教清微派宗派第十六代法師，中國黃龍仙派玄元宗壇天梯先鋒官。林元陽本名林志堅，也是純易神卦的創始人，通曉獨創的「聽音占卜」，即透過當下所聞聲音可占卜預知。林道長特別崇拜仙組呂洞賓，他認為呂祖是一位精於卦理的祖師，亦是明朝至今求神問卜的主流神仙。呂仙祖的服飾、聖座也充滿了卦象，所以林道長創立的「純易神卦」派。他自稱「亦因太崇拜仙祖，不敢直呼『純陽』名諱，而取名『純易』。而這聽音的耳朵旁，加到『易』字上，便是『陽』，『純陽神卦』的隱藏版。」林道長以熟悉「聽音占卜」的卜卦方式，為人服務。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在道務方面，林道長與多位同門高功道長經常合作，以清微道法為信眾服務，所執行的業務橫跨陰陽法事，即有宮廟的禮斗法會、作醮吉事，亦有民間喪事的齋儀。道長林元陽因對道教科儀有獨特的濃厚興趣，閒暇之餘對道藏中科儀內涵進行深入的理解與研讀，故經常有獨到的心得與見解，也因此激盪出一些科儀的新內涵及獨門詮釋，可說是道教神職人員中，除了專長科儀法事能力外，對道藏經典科儀的研讀與儀式詮釋的實際運用，常有自己的創意與見解。林道長曾師承清微總壇的老師周清林道長多年，周道長以前也拜過全真師父，學過全真的曲子，故林道長也從其師父處學過一首全真的曲子，目前也用在他自己清微道壇的科儀中。林道長因接辦宮廟法會之故，常接觸到宮廟中的靈山派宮主、人士或信徒，也常被對方提出新的儀式想法要求。這部分是原本清微科儀中少見或未有的科儀內涵。為了滿足對方要求，一再從道藏中找出科儀的內涵與依據，安排或增補科儀的新章法，進行對方所需的特殊祭儀活動，以達到法事的圓滿。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;此次法會取名「一元復始護國消災星漢普照祈安植福大聖會」共九天。從名稱上也少見，其科儀儀式上的作法，也很多自創，包括法會主辦單位三芝瑤聖宮宮主張雪娥女士，如何事前透過其主神「五申大慈尊」（或「無極五母大慈尊」）託夢給宮主的一些啟示，如法會的日期、法會的名稱、會場中需準備的一些祭品、供品；其中較為特殊的是：建架一艘彩色「五母船」、油輪船一艘（灰色）、一艘飛碟（圓形）、多樓層遊輪一艘（白色）、西拉雅阿立祖的茅草屋一間等。這幾個物件的構法與造型，都是張宮主從母娘給的靈感指示中勾畫出的草圖，再請人做出實物，放置於會場。另外，林道長建議以臺灣立體地圖（綠色）含中央山脈與西部平原作為所護國的實物與象徵，並在現場法會儀式過程中，進行對臺灣地圖點光明燈儀式。類似諸如此類的法會儀式、供品、供物，確實在其他道壇的法會上少見。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法會的頭三天與後四天由清微「純陽宗壇」負責科演；中間的兩天（10/15-16）則由主辦單位「瑤聖宮」廟方信徒負責誦經。「純陽宗壇」在法會會場除了掛上該壇的壇號外，也掛上了「祇迓鴻庥」（恭迎神恩降臨之意）與「祇度津梁」（恭敬度化有緣眾生之意）兩幅橫布匾掛；這是林志堅道長自己所發明的用詞，也是其他清微道壇少見的。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法會的最後一天，午前，有一個犒軍科儀，即犒賞五營兵馬，這也是一般法會結束前常見的一個小法儀式，由道長以小法的服飾與裝扮，進行對該地五營兵馬守衛鄰里的謝意。法會開始時，先邀請五營兵馬前來接受犒賞、共享盛宴。盛宴結束後，請他們速回歸其原位，不要滯留人間。負責主持小法的道士兼法師，並未著一般常見的紅頭法師服，而是在道服外面繫上一條龍虎群。該道士頭部仍是戴道士的道帽，部分也以法師龍虎裙的裝扮，吹牛角，但不使用蛇鞭（法索），只象徵性的擺在桌上，據林道長的說法是，不忍用蛇鞭暴力鞭打在無形的身上，違反慈悲的理念。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;法會期間可以看到不少，或大部分為靈山派信徒前來拜會或共襄盛舉。而瑤聖宮也因為場地寬闊的原因，在外場的多處空地上，張宮主允許全國各地的靈山派人士或信徒，前來使用；故也可看到他們所擺設的各種以不同顏色的紙花、水瓶、象徵物，呈現出各種不同的造型。靈山派人士不停地在其間靈動，或情感流露自我吟唱，或形同忘我，出現一些肢體上的各種動作，不一而足；或有類似藏傳習俗的煙供、現煮中藥茶的藥引（醫治無形）可謂創意十足，不是一般傳統正規法會可見到的場景。</p>
