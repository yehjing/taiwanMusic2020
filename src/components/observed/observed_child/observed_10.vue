      <strong>二、保童女神「三奶夫人」在道、法各派之不同的科儀詮釋：</strong>
    </p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019「順天聖母臨水夫人陳靖姑文化節」之「栽花換斗」、「過平安橋」；臺南菱洲宮「協和堂」閭山小法團之「過平安橋」；臺南「和勝堂」小法團之「過百花橋」；與清微道派三奶夫人之「過關保童」科儀</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本節幾個道教或法教的儀式，如「栽花換斗」、「過平安橋」、「過百花橋」，或「過關保童」都是直接或間接與臨水夫人，或稱三奶夫人有關的儀式。「過平安橋」（或稱七星平安橋），或「百花橋」（或「七星百花橋」）都是臺灣常見的小法儀式。其主要的目的，是經過法師加持儀式之後的橋，讓信徒走過此橋（平安橋），可求得平安，或接財神（如臺南和勝堂）；或透過道長儀式加持之後，得平安地渡過人生三十六個險惡關卡。「百花橋」名稱的源由，是在七星橋下的左右兩邊，置滿各種紅、白顏色的鮮花植物，隱喻百花。「過七星平安橋」的用詞，指的是橋面劃上符令與七星罡訣；或在橋面覆上一塊黑布，上面畫有符字及七星罡訣，或鋪上五色布加花色布橋欄杆貼相關符咒，橋下用火爐或油燈排七星燈等，讓信徒得以渡過關限、消災解厄，祈求平安。</p>
