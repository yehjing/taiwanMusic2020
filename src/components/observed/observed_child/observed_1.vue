    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;門頭【倍工】屬七撩大曲，【北相思】、【竹馬兒】、【沙淘金】、【叠韻悲】等門頭也是三撩拍的功夫曲，僅有【雙閨】為一二拍經常演唱的門頭，而本次安排了8首【雙閨】曲，相同的門頭大韻旋律樂曲要唱8遍，順序越後面的唱曲吸引力有逐漸遞減的現象。本次報曲的館閣僅有台南市南聲社、詠吟南樂社、鹿港聚英社、鹿港遏雲齋、台北華聲南樂團、和鳴南樂社、振聲社、彰化縣文化局南管實驗樂團、長隆南樂社、清雅樂府以及黃瑤慧絃友等，大家幾乎都是學習新曲來參與演唱。以排門頭進行整絃是較具有曲目實踐保存的意義，只是新曲需要時間精煉韻味，特別是三撩拍、七撩拍等上撩的功夫曲更是如此，除了唱曲曲腳必須樂曲熟練，也需要傢俬腳的演奏技藝烘襯，如此才能更圓滿排門頭整絃的意義。</p>
