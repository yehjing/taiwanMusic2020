    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;整絃會奏的程序為起指、落曲、煞譜，活動開始通常以十音編制演奏噯子指，第一首噯子指由主辦館閣演奏或邀請各館閣一同合奏，接著主辦館閣演奏上四管編制的簫指，然後起曲之後，再由前來參加的館閣接續唱曲，最後煞譜結束整絃。在簫指之前或唱曲過程中，也可再安排噯子指的十音會奏，如4/20南聲社春祭，在第一首〈朱郎卜返〉之後，又連續安排彰化縣文化局南管實驗樂團《汝因勢》以及絃友一同的〈魚沉〉，然後才演奏簫指。此外，在主辦館閣同意之下，也可再演奏簫指，如10/19南聲社秋祭，在海寮清和社演唱〈暗想暗猜〉之後，絃友一同合奏簫指〈月色卜落〉，只是這樣的情況較為罕見，因為較不符合一般整絃的程序。觀察今年度的整絃活動，可見社區大學研習班或館閣研習班的學員也陸續積極參與館閣祭典整絃大會，如大同社區大學南管班參與和鳴南樂社、鹿港聚英社整絃；高雄歷史博物館傳習班參與南聲社春、秋祭整絃；中華絃管也帶領松山社區大學南管班學員參與基隆市閩南第一樂團秋祭整絃、南聲社秋祭整絃。帶領初學或學習資歷尚淺的學員參與整絃是很好的觀摩機會，但若要報曲演唱，可能人選還是要斟酌挑選較好。</p>
