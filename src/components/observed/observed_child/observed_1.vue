    <p>2.漢唐樂府：南音古典樂舞戲《百世大儒－陸九淵》</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;漢唐樂府雖然自2011年南管編鐘樂舞《盤之古》演出之後，近幾年甚少在臺灣展露，但仍堅持梨園樂舞創作的理念。2019年受中國江西省金溪縣政府委託製作《百世大儒－陸九淵》，參與首屆象山文化旅遊節暨陸象山誕辰880週年紀念活動，於10/25-27日於江西省金溪縣陸九淵文化藝術中心。</p>
