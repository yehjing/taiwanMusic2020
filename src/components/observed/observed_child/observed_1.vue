    <p>（二）胡建智為合和藝苑館員，也是「重要傳統表演藝術南管戲曲－林吳素霞傳習計畫」第二屆藝生。胡建智於5/16病故，合和藝苑於6/1在其追思奠禮公祭中以絃管祭儀式奏唱〈三奠酒〉祀套奠祭。年輕生命，享年39歲。文化部文資局也於奠禮中頒發「重要傳統表演藝術傳習修業證書」。</p>
