    <h3>
      <strong>結語</strong>
    </h3>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2019年南管音樂相關活動，大大小小場次近400場，不僅館閣用心傳習推廣，政府公部門也挹注經費補助支持以及舉辦相關活動，安排展露演出；其中彰化縣政府文化局更是積極推動在地南北管音樂戲曲的傳承與展演。只是各館閣開設研習專班，經費幾乎全由政府補助，館閣減少了自身經費籌謀規劃而依賴公部門支持，這樣的狀況對於館閣長遠發展是否有持續且正向的助益，值得後續觀察。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;今年度的中小學的南管傳習有新增臺南永康國小，社區大學也有擴點開新班，指導老師也多來自館閣長期培養的中生代、新生代，指導初學、初階師資人才逐漸增加，對於南管未來發展是非常好的現象，但教導中階、高階的師資培養速度，卻遠不及高階資深藝師的凋零，這是很令人擔憂的地方。而北藝大傳音系主辦的「新綠妍萌－青少年南管音樂會」因經費問題，僅邀請6個館閣、學校參加，引發部分未受邀單位的抱怨，希望再有機會可邀請他們參加，可見讓青少年相聚的專場音樂會有激勵之效，也期待北藝大傳音系能續辦下去，讓立意良好的音樂會有延展的可能。另外，汾雅齋因為曾參與心心南管樂坊的王新源加入，將其曾參加心心南管樂坊的演出經驗以及劇場式美學觀提供汾雅齋，所以今年度於北港文化中心家湖表演廳、故宮南院集賢廳演出，在節目設計安排、舞臺擺設與傳統館閣演出稍有不同；不過，汾雅齋開始走出館閣，主動步向正式演藝場所，展現南管音樂演出能量，是值得肯定與可喜的事。至於國際交流部分，諸如南音大匯演此類的交流活動，泉州年年必辦，臺灣館閣受邀前往，有交通費用、交際往來的禮品支出的經費問題，加上旅途勞頓、活動安排是否具有吸引力等等，都是考量是否前往參與的因素。據觀察，臺灣館閣參與意願已不似往年熱絡。據聞2020年5月即將在馬來西亞登場的「第四屆世界南音聯誼會－南音大匯演」，屆時應邀的臺灣館閣是否都會前往參加？則有待觀察！</p>
